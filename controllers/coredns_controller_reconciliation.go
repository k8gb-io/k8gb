type CoreDNSReconciler struct {
	client.Client
	Config      *resolver.Config
	Recorder    record.EventRecorder
	Tracer      trace.Tracer
	Scheme      *runtime.Scheme
	DNSProvider dns.Provider
	Bootstrap   *bootstrap.Bootstrap
	ZoneService zones.ZoneService
}

func (r *CoreDNSReconciler) Reconcile(ctx context.Context, req ctrl.Request) (ctrl.Result, error) {
	// todo: introduce variable for reconciliation interval
	var err error
	result := utils.NewReconcileResultHandler(0)

	z, err := r.ZoneService.List(ctx)
	if err != nil {
		logger.Err(err).Msg("Error listing zones")
		return result.Requeue()
	}
	logger.Info().Msgf("Reconciling '%s' %s", req, z.ReadIPs())

	for _, zoneInfo := range z {
		err = r.DNSProvider.CreateZoneDelegation(zoneInfo)
		if err != nil {
			logger.Err(err).
				Str("loadBalancedZone", zoneInfo.LoadBalancedZone).
				Str("IPs", strings.Join(zoneInfo.IPs, ",")).
				Msg("Error creating zone delegation")
			return result.Requeue()
		}
	}

	return result.Stop()
}
