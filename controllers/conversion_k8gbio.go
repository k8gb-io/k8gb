import (
	"reflect"

	k8gbv1beta1 "github.com/k8gb-io/k8gb/api/v1beta1"
	k8gbv1beta1io "github.com/k8gb-io/k8gb/api/v1beta1io"
	corev1 "k8s.io/api/core/v1"
	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"
)

const migrationLabelKey = "k8gb.io/migrated-to-k8gb-io"

func convertGslbLegacyToIO(gslb *k8gbv1beta1.Gslb) *k8gbv1beta1io.Gslb {
	if gslb == nil {
		return &k8gbv1beta1io.Gslb{}
	}

	return &k8gbv1beta1io.Gslb{
		TypeMeta: metav1.TypeMeta{
			APIVersion: k8gbv1beta1io.GroupVersion.String(),
			Kind:       "Gslb",
		},
		ObjectMeta: metav1.ObjectMeta{
			Name:      gslb.Name,
			Namespace: gslb.Namespace,
		},
		Spec: convertSpecLegacyToIO(gslb),
	}
}

func convertGslbLegacyToIORuntime(gslb *k8gbv1beta1.Gslb) *k8gbv1beta1io.Gslb {
	if gslb == nil {
		return &k8gbv1beta1io.Gslb{}
	}

	return &k8gbv1beta1io.Gslb{
		TypeMeta: metav1.TypeMeta{
			APIVersion: k8gbv1beta1io.GroupVersion.String(),
			Kind:       "Gslb",
		},
		ObjectMeta: metav1.ObjectMeta{
			Name:      gslb.Name,
			Namespace: gslb.Namespace,
		},
		Spec: k8gbv1beta1io.GslbSpec{
			Ingress:     k8gbv1beta1io.FromV1IngressSpec(k8gbv1beta1.ToV1IngressSpec(gslb.Spec.Ingress)),
			Strategy:    convertStrategyLegacyToIO(gslb.Spec.Strategy),
			ResourceRef: convertResourceRefLegacyToIO(gslb.Spec.ResourceRef),
		},
	}
}

func convertSpecLegacyToIO(gslb *k8gbv1beta1.Gslb) k8gbv1beta1io.GslbSpec {
	if gslb == nil {
		return k8gbv1beta1io.GslbSpec{}
	}

	spec := gslb.Spec
	converted := k8gbv1beta1io.GslbSpec{
		Ingress:     k8gbv1beta1io.FromV1IngressSpec(k8gbv1beta1.ToV1IngressSpec(spec.Ingress)),
		Strategy:    convertStrategyLegacyToIO(spec.Strategy),
		ResourceRef: convertResourceRefLegacyToIO(spec.ResourceRef),
	}

	if reflect.DeepEqual(spec.ResourceRef, k8gbv1beta1.ResourceRef{}) &&
		!reflect.DeepEqual(spec.Ingress, k8gbv1beta1.IngressSpec{}) {
		converted.ResourceRef = k8gbv1beta1io.ResourceRef{
			ObjectReference: corev1.ObjectReference{
				APIVersion: "networking.k8s.io/v1",
				Kind:       "Ingress",
				Name:       gslb.Name,
				Namespace:  gslb.Namespace,
			},
		}
		converted.Ingress = k8gbv1beta1io.IngressSpec{}
	}

	return converted
}

func convertStrategyLegacyToIO(strategy k8gbv1beta1.Strategy) k8gbv1beta1io.Strategy {
	return k8gbv1beta1io.Strategy{
		Type:                       strategy.Type,
		Weight:                     strategy.Weight,
		PrimaryGeoTag:              strategy.PrimaryGeoTag,
		DNSTtlSeconds:              strategy.DNSTtlSeconds,
		SplitBrainThresholdSeconds: strategy.SplitBrainThresholdSeconds,
	}
}

func convertResourceRefLegacyToIO(ref k8gbv1beta1.ResourceRef) k8gbv1beta1io.ResourceRef {
	return k8gbv1beta1io.ResourceRef{
		ObjectReference: ref.ObjectReference,
		LabelSelector:   ref.LabelSelector,
	}
}
