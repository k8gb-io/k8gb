type ZoneDelegationReconciler struct {
	client.Client
	Config      *resolver.Config
	Recorder    record.EventRecorder
	Tracer      trace.Tracer
	Scheme      *runtime.Scheme
	DNSProvider dns.Provider
	ZoneService zones.ZoneDelegation
	IPResolver  ipresolver.Resolver
	Logger      *zerolog.Logger
}

const zoneDelegationFinalizer = "k8gb.io/finalizer"

func (r *ZoneDelegationReconciler) Reconcile(ctx context.Context, req ctrl.Request) (ctrl.Result, error) {
	var err error
	result := utils.NewReconcileResultHandler(r.Config.ReconcileRequeueSeconds)
	if !r.ZoneService.HasAvailableIPs(ctx) {
		r.Logger.Info().
			Str("name", req.Name).
			Msg("Waiting for available IPs. Skipping ZoneDelegation reconciliation")
		return result.Requeue()
	}

	zone, err := r.ZoneService.Get(ctx, req.NamespacedName)
	if err != nil {
		if errors.IsNotFound(err) {
			return result.Requeue()
		}
		r.Logger.Err(err).
			Str("name", req.Name).
			Msg("Error processing ZoneDelegation")
		return result.RequeueError(err)
	}

	err = r.ensureFinalizer(ctx, zone)
	if err != nil {
		r.Logger.Err(err).
			Str("name", zone.Name).
			Msg("Error ensuring finalizer")
		return result.RequeueError(err)
	}

	if zone.DeletionTimestamp != nil {
		err := r.finalize(ctx, zone)
		if err != nil {
			return result.RequeueError(err)
		}
		return result.Stop()
	}

	err = r.ZoneService.UpdateStatus(ctx, zone)
	if err != nil {
		r.Logger.Err(err).Str("Name", zone.Name).Msg("Error updating zone delegation")
		return result.RequeueError(err)
	}

	r.Logger.Info().
		Str("Name", zone.Name).
		Msg("Finished Reconciling zone delegation")
	return result.Requeue()
}

func (r *ZoneDelegationReconciler) finalize(ctx context.Context, zone *v1beta1.ZoneDelegation) error {
	if zone.Spec.DoFinalize {
		zoneDetail, err := r.ZoneService.ExtendedZoneDelegation(ctx, zone)
		if err != nil {
			return err
		}
		err = r.DNSProvider.Finalize(zoneDetail, true)
		if err != nil {
			return err
		}
	}
	return r.removeFinalizer(ctx, zone)
}

func (r *ZoneDelegationReconciler) removeFinalizer(ctx context.Context, zone *v1beta1.ZoneDelegation) error {
	if !controllerutil.ContainsFinalizer(zone, zoneDelegationFinalizer) {
		return nil
	}

	controllerutil.RemoveFinalizer(zone, zoneDelegationFinalizer)

	if err := r.Update(ctx, zone); err != nil {
		return fmt.Errorf("failed to remove ZoneDelegation finalizer: %w", err)
	}

	return nil
}

func (r *ZoneDelegationReconciler) ensureFinalizer(ctx context.Context, zone *v1beta1.ZoneDelegation) error {
	if controllerutil.ContainsFinalizer(zone, zoneDelegationFinalizer) {
		return nil
	}
	controllerutil.AddFinalizer(zone, zoneDelegationFinalizer)
	if err := r.Update(ctx, zone); err != nil {
		return fmt.Errorf("failed to add ZoneDelegation finalizer: %w", err)
	}
	return nil
}
