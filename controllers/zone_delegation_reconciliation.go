type ZoneDelegationReconciler struct {
	client.Client
	Config      *resolver.Config
	Recorder    record.EventRecorder
	Tracer      trace.Tracer
	Scheme      *runtime.Scheme
	DNSProvider dns.Provider
	ZoneService zones.ZoneDelegation
	IPResolver  ipresolver.Resolver
	Logger      *zerolog.Logger
}

func (r *ZoneDelegationReconciler) Reconcile(ctx context.Context, req ctrl.Request) (ctrl.Result, error) {
	var err error
	result := utils.NewReconcileResultHandler(r.Config.ReconcileRequeueSeconds)
	if !r.ZoneService.HasAvailableIPs(ctx) {
		r.Logger.Info().
			Str("name", req.Name).
			Msg("Waiting for available IPs. Skipping ZoneDelegation reconciliation")
		return result.Requeue()
	}

	zone, err := r.ZoneService.Get(ctx, req.NamespacedName)
	if err != nil {
		r.Logger.Err(err).
			Str("name", req.Name).
			Msg("Error processing ZoneDelegation")
		return result.RequeueError(err)
	}

	if zone.IsInDeletion() {
		r.Logger.Info().Str("name", zone.Name).Msg("Deleting ZoneDelegation")
		err = r.RemoveZoneDelegation(ctx, zone)
		if err != nil {
			r.Logger.Err(err).
				Str("name", zone.Name).
				Str("provider", r.DNSProvider.String()).
				Msg("Deleting ZoneDelegation")
			return result.RequeueError(err)
		}
		return result.Stop()
	}

	err = r.ZoneService.UpdateStatus(ctx, zone)
	if err != nil {
		r.Logger.Err(err).Str("Name", zone.Name).Msg("Error updating zone delegation")
		return result.RequeueError(err)
	}

	return result.Requeue()
}

func (r *ZoneDelegationReconciler) RemoveZoneDelegation(ctx context.Context, zone *v1beta1.ZoneDelegation) error {
	isLastAliveCluster := func(zd *v1beta1.ZoneDelegation) bool {
		current := r.Config.GetNsName(zd.Spec.LoadBalancedZone, zd.Spec.ParentZone)
		for _, s := range zd.Status.DNSServers {
			if s.Name != current {
				return false
			}
		}
		return true
	}

	// remove zone delegation from local cluster
	err := r.ZoneService.RemoveZoneDelegation(ctx, zone)
	if err != nil {
		return err
	}

	// We determine whether we are the last available cluster.
	canDeleteZoneDelegation := isLastAliveCluster(zone)
	err = r.DNSProvider.Finalize(zone, canDeleteZoneDelegation)
	if err != nil {
		return err
	}
	return nil
}
