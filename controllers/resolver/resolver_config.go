import (
	"github.com/alecthomas/kong"
	"github.com/k8gb-io/k8gb/api/k8gb.io/v1beta1"
)

func (r *Resolver) ResolveOperatorConfig() (cfg *Config, err error) {
	cfg = &Config{}
	_ = kong.Parse(cfg)
	var zoneInfo = []v1beta1.ZoneDelegationSpec{}
	if !cfg.DynamicZones {
		zoneInfo, err = getZoneConfigFromString(cfg.DNSZones)
		if err != nil {
			return nil, err
		}
	}
	cfg.DelegationZones, err = ParseDelegationZones(cfg, zoneInfo)
	if err != nil {
		return nil, err
	}
	cfg.EdgeDNSType, _ = cfg.getEdgeDNSType()
	cfg.ParentZoneDNSServers = cfg.parseParentZoneDNSServers()

	// Run validations
	err = r.configValidator.Struct(cfg)
	if err != nil {
		return nil, err
	}
	return cfg, err
}

func (c *Config) HasExtClusterGeoTags() bool {
	if len(c.DelegationZones) == 0 {
		return false
	}
	return len(c.DelegationZones[0].ExtClusterNSNames) > 0
}
