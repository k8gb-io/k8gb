import (
	"os"
)

func (r *Resolver) GetDeprecations() []string {
	var deprecations []string
	deprecationsMap := map[string]string{}
	deprecationsMap["EDGE_DNS_SERVER"] = "EDGE_DNS_SERVER has been deprecated use EDGE_DNS_SERVERS instead"
	deprecationsMap["EDGE_DNS_SERVER_PORT"] = "EDGE_DNS_SERVER_PORT has been deprecated use EDGE_DNS_SERVERS instead"
	for k, v := range deprecationsMap {
		if os.Getenv(k) != "" {
			deprecations = append(deprecations, v)
		}
	}
	return deprecations
}
