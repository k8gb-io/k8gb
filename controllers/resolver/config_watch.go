// IsWatchedNamespace checks if the given namespace is the one being handled by k8gb
func (c *Config) IsWatchedNamespace(namespace string) bool {
	// watched namespaces not set, watch all namespaces
	if len(c.WatchedNamespaces) == 0 {
		return true
	}
	// check if the given namespace is in the list of watched namespaces
	for _, ns := range c.WatchedNamespaces {
		if ns == namespace {
			return true
		}
	}
	return false
}

func (c *Config) formatWatchedNamespaces() []string {
	var wns = []string{}
	for _, ns := range c.WatchedNamespaces {
		wns = append(wns, strings.ReplaceAll(ns, " ", ""))
	}
	return wns
}
