import (
	"context"
	"fmt"

	netv1 "k8s.io/api/networking/v1"
	"sigs.k8s.io/controller-runtime/pkg/client"
)

type Ingress struct {
	client  client.Client
	context context.Context
}

const IngressLabelName = "k8gb.io/ip-source"
const IngressLabelValue = "true"

var ingressLabel = fmt.Sprintf("%s=%s", IngressLabelName, IngressLabelValue)

func NewIngressAssistant(context context.Context, client client.Client) *Ingress {
	return &Ingress{context: context, client: client}
}

// GetResource reads first ingress having the ingressLabel
func (r *Ingress) GetResource() (*netv1.Ingress, error) {
	ingressList, err := getObjectList(r.context, r.client, &netv1.IngressList{}, ingressLabel, "")
	if err != nil {
		return nil, err
	}
	if len(ingressList.Items) == 0 {
		return nil, fmt.Errorf("ingress with label %s was not found", ingressLabel)
	}
	ingresses := &ingressList.Items[0]
	return ingresses, nil
}

func (r *Ingress) GetExposedIPs() (ips []string, ingress *netv1.Ingress, err error) {
	ingress, err = r.GetResource()
	if err != nil {
		return nil, nil, fmt.Errorf("finding ingress (%s)", err)
	}
	for _, lb := range ingress.Status.LoadBalancer.Ingress {
		if lb.IP != "" {
			ips = append(ips, lb.IP)
		}
	}
	return ips, ingress, nil
}
