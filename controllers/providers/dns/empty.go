import (
	"github.com/k8gb-io/k8gb/controllers/resolver"
)

// EmptyDNSProvider is executed when fakeDNSEnabled is true.
type EmptyDNSProvider struct {
	config resolver.Config
}

func NewEmptyDNS(config resolver.Config) *EmptyDNSProvider {
	return &EmptyDNSProvider{
		config: config,
	}
}

func (p *EmptyDNSProvider) CreateZoneDelegation(_ *resolver.DelegationZoneInfo) (err error) {
	return nil
}

func (p *EmptyDNSProvider) Finalize(_ *resolver.DelegationZoneInfo, _ bool) (err error) {
	return nil
}

func (p *EmptyDNSProvider) String() string {
	return "EMPTY"
}
