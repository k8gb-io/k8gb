import (
	"fmt"

	"github.com/k8gb-io/k8gb/api/v1beta1io"
	"github.com/k8gb-io/k8gb/controllers/ipresolver"
	"github.com/k8gb-io/k8gb/controllers/resolver"
	"github.com/k8gb-io/k8gb/controllers/utils"
)

type DynamicResolver struct {
	config *resolver.Config
}

func NewDynamicResolver(config *resolver.Config) *DynamicResolver {
	return &DynamicResolver{config: config}
}

func (r *DynamicResolver) Resolve(zone *v1beta1io.ZoneDelegation) (ipresolver.ClusterNSNames, error) {
	var err error
	var parentNameServer *utils.DNSServer
	var found bool

	// with the DNS server. If you only have a few calls per reconciliation, that's fine — but if your infrastructure contains
	// thousands of GSLBs, you can overwhelm the DNS. If the DNS is overwhelmed, you need to set the external
	// GeoTags manually.
	if parentNameServer, found = r.config.ParentZoneDNSServers.Random(); !found {
		return nil, nil
	}

	// 1.st dig
	parentDNSServer, err := ipresolver.ExtractParentDNSServer(parentNameServer, zone.Spec.ParentZone)
	if err != nil {
		return nil, fmt.Errorf("ExternalGeoTags: error extracting parent DNS servers: %w", err)
	}

	// 2.nd dig
	extClusterNSNames, err := ipresolver.DiscoverNameServers(parentDNSServer, zone.Spec.LoadBalancedZone)
	if err != nil {
		return extClusterNSNames, fmt.Errorf("ExternalGeoTags: reading geo tags: %w", err)
	}

	// caution: This ultimately gets called during every reconciliation. Each call to dig means an interaction
	return extClusterNSNames, nil
}
