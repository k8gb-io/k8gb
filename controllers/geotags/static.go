import (
	"context"

	"github.com/k8gb-io/k8gb/controllers/bootstrap"
	"github.com/k8gb-io/k8gb/controllers/resolver"
	"github.com/k8gb-io/k8gb/controllers/zones"
)

type Static struct {
	config    *resolver.Config
	svc       zones.ZoneDelegation
	bootstrap bootstrap.BoundIPsService
}

func NewStatic(config *resolver.Config, svc zones.ZoneDelegation, bootstrap bootstrap.BoundIPsService) *Static {
	return &Static{
		config:    config,
		svc:       svc,
		bootstrap: bootstrap,
	}
}

func (s *Static) GetExternalClusterNSNamesByHostname(ctx context.Context, host string) (map[string]string, error) {
	list, err := s.svc.List(ctx)
	if err != nil {
		return nil, err
	}
	z := getZone(list, host)
	if z != nil {
		detail, err := zones.NewZoneDelegationWrapper(z, s.config, s.bootstrap).GetDetail(ctx)
		if err != nil {
			return nil, err
		}
		return detail.ExtClusterNSNames, nil
	}
	return map[string]string{}, nil
}
