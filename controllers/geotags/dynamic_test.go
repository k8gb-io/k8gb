const (
	server = "localhost"
	port   = 8053
)

func TestResolution(t *testing.T) {
	parentDNS1 := utils.DNSServer{
		Host: server,
		Port: port,
	}

	settings := utils.FakeDNSSettings{
		FakeDNSPort:     parentDNS1.Port,
		EdgeDNSZoneFQDN: "example.com.",
		DNSZoneFQDN:     "cloud.example.com.",
	}
	fakeDNS := utils.NewFakeDNS(settings)
	fakeDNS.
		// First lookup:
		// dig NS example.com
		AddNSRecord("example.com.", "ns.example.com.").
		AddExtraARecord("example.com.", "ns.example.com.", net.IPv4(127, 0, 0, 1)).
		// Second lookup:
		// dig NS cloud.example.com
		AddNSRecord("cloud.example.com.", "gslb-ns-us-cloud.example.com.").
		AddNSRecord("cloud.example.com.", "gslb-ns-eu-cloud.example.com.").
		Start().
		RunTestFunc(func() {
			testResolution(t, parentDNS1)
		}).RequireNoError(t)
}

func testResolution(t *testing.T, parentDNS utils.DNSServer) {
	tests := []struct {
		name          string
		config        *resolver.Config
		expectedError bool
		result        ipresolver.ClusterNSNames
		zone          *v1beta1io.ZoneDelegation
	}{
		{
			name: "static: resolve all clusters",
			zone: &v1beta1io.ZoneDelegation{
				Spec: v1beta1io.ZoneDelegationSpec{
					LoadBalancedZone: "cloud.example.com",
					ParentZone:       "example.com",
				},
			},
			config: &resolver.Config{
				ClusterGeoTag:         "us",
				ExtClustersGeoTagsRaw: []string{"eu"},
				EdgeDNSType:           resolver.DNSTypeInfoblox,
				ParentZoneDNSServers:  []utils.DNSServer{parentDNS},
			},
			expectedError: false,
			result: map[string]string{
				"gslb-ns-eu-cloud.example.com": "eu",
				"gslb-ns-us-cloud.example.com": "us",
			},
		},
		{
			name: "static: resolve itself",
			zone: &v1beta1io.ZoneDelegation{
				Spec: v1beta1io.ZoneDelegationSpec{
					LoadBalancedZone: "cloud.example.com",
					ParentZone:       "example.com",
				},
			},
			config: &resolver.Config{
				ClusterGeoTag:         "us",
				ExtClustersGeoTagsRaw: []string{},
				EdgeDNSType:           resolver.DNSTypeExternal,
				ParentZoneDNSServers:  []utils.DNSServer{parentDNS},
			},
			expectedError: false,
			result: map[string]string{
				"gslb-ns-us-cloud.example.com": "us",
			},
		},
		{
			name: "dynamic: resolve all clusters",
			zone: &v1beta1io.ZoneDelegation{
				Spec: v1beta1io.ZoneDelegationSpec{
					LoadBalancedZone: "cloud.example.com",
					ParentZone:       "example.com",
				},
			},
			config: &resolver.Config{
				ClusterGeoTag:         "us",
				ExtClustersGeoTagsRaw: nil,
				EdgeDNSType:           resolver.DNSTypeInfoblox,
				ParentZoneDNSServers:  []utils.DNSServer{parentDNS},
			},
			expectedError: false,
			result: map[string]string{
				"gslb-ns-eu-cloud.example.com": "eu",
				"gslb-ns-us-cloud.example.com": "us",
			},
		},

		{
			name: "dynamic: inaccessible DNS",
			zone: &v1beta1io.ZoneDelegation{
				Spec: v1beta1io.ZoneDelegationSpec{
					LoadBalancedZone: "cloud.example.com",
					ParentZone:       "example.com",
				},
			},
			config: &resolver.Config{
				ClusterGeoTag:         "us",
				ExtClustersGeoTagsRaw: nil,
				EdgeDNSType:           resolver.DNSTypeInfoblox,
				ParentZoneDNSServers:  []utils.DNSServer{{Host: "127.0.0.1", Port: 8055}},
			},
			expectedError: true,
			result:        nil,
		},
	}
	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {
			result, err := Provide(test.config).Resolve(test.zone)
			if test.expectedError {
				require.Error(t, err)
				return
			}
			require.NoError(t, err)
			assert.True(t, maps.Equal(test.result, result))
		})
	}
}
