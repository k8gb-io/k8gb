type GeoTags interface {
	GetExternalClusterNSNamesByHostname(host string) (map[string]string, error)
}

func getZone(config *depresolver.Config, host string) *depresolver.DelegationZoneInfo {
	for _, z := range config.DelegationZones {
		if strings.Contains(host, z.ParentZone) {
			return z
		}
	}
	return nil
}

func GeoTag(config *depresolver.Config) GeoTags {
	if config.HasExtClusterGeoTags() {
		return NewStatic(config)
	}
	return NewDynamic(config)
}
