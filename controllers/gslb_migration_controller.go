type LegacyGslbMigrationReconciler struct {
	client.Client
	Recorder record.EventRecorder
}

const legacyMigrationFinalizer = "k8gb.io/legacy-migration-protection"
const migrationRequestLabelKey = "k8gb.io/migration-requested"

func (r *LegacyGslbMigrationReconciler) Reconcile(ctx context.Context, req ctrl.Request) (ctrl.Result, error) {
	legacy := &k8gbv1beta1.Gslb{}
	if err := r.Get(ctx, req.NamespacedName, legacy); err != nil {
		if apierrors.IsNotFound(err) {
			return ctrl.Result{}, nil
		}
		return ctrl.Result{}, err
	}

	if legacy.DeletionTimestamp != nil {
		if err := r.detachLegacyEmbeddedIngressOwner(ctx, legacy); err != nil {
			return ctrl.Result{}, err
		}
		if err := r.detachLegacyDNSEndpointOwner(ctx, legacy, nil); err != nil {
			return ctrl.Result{}, err
		}
		if err := r.removeMigrationFinalizer(ctx, legacy); err != nil {
			return ctrl.Result{}, err
		}
		return ctrl.Result{}, nil
	}

	migrated := hasTrueLabel(legacy.Labels, migrationLabelKey)
	requested := hasTrueLabel(legacy.Labels, migrationRequestLabelKey)
	if migrated {
		if err := r.ensureMigrationFinalizer(ctx, legacy); err != nil {
			return ctrl.Result{}, err
		}
		if err := r.detachLegacyEmbeddedIngressOwner(ctx, legacy); err != nil {
			return ctrl.Result{}, err
		}
		canonical, err := r.getCanonicalGslb(ctx, legacy)
		if err != nil {
			return ctrl.Result{}, err
		}
		if err := r.detachLegacyDNSEndpointOwner(ctx, legacy, canonical); err != nil {
			return ctrl.Result{}, err
		}
		r.Recorder.Eventf(
			legacy,
			corev1.EventTypeWarning,
			"LegacyIgnored",
			"Legacy Gslb ignored. Edit k8gb.io Gslb %s/%s instead.",
			legacy.Namespace,
			legacy.Name,
		)
		return ctrl.Result{}, nil
	}
	if !requested {
		if err := r.removeMigrationFinalizer(ctx, legacy); err != nil {
			return ctrl.Result{}, err
		}
		r.Recorder.Eventf(
			legacy,
			corev1.EventTypeWarning,
			"LegacyDeprecated",
			"Legacy Gslb %s/%s uses deprecated API group k8gb.absa.oss. Set label %s=true to migrate to k8gb.io.",
			legacy.Namespace,
			legacy.Name,
			migrationRequestLabelKey,
		)
		return ctrl.Result{}, nil
	}
	if err := r.ensureMigrationFinalizer(ctx, legacy); err != nil {
		return ctrl.Result{}, err
	}

	desired := convertGslbLegacyToIO(legacy)
	createdCanonical := false

	existing := &k8gbv1beta1io.Gslb{}
	if err := r.Get(ctx, types.NamespacedName{Name: legacy.Name, Namespace: legacy.Namespace}, existing); err != nil {
		if apierrors.IsNotFound(err) {
			if err := r.Create(ctx, desired); err != nil {
				return ctrl.Result{}, err
			}
			createdCanonical = true
		} else {
			return ctrl.Result{}, err
		}
	}

	if err := r.detachLegacyEmbeddedIngressOwner(ctx, legacy); err != nil {
		return ctrl.Result{}, err
	}
	canonical, err := r.getCanonicalGslb(ctx, legacy)
	if err != nil {
		return ctrl.Result{}, err
	}
	if err := r.detachLegacyDNSEndpointOwner(ctx, legacy, canonical); err != nil {
		return ctrl.Result{}, err
	}

	legacyPatch := client.MergeFrom(legacy.DeepCopy())
	if legacy.Labels == nil {
		legacy.Labels = map[string]string{}
	}
	legacy.Labels[migrationLabelKey] = "true"
	if err := r.Patch(ctx, legacy, legacyPatch); err != nil {
		return ctrl.Result{}, err
	}

	if createdCanonical {
		r.Recorder.Eventf(
			legacy,
			corev1.EventTypeNormal,
			"LegacyMigrated",
			"Legacy Gslb migrated to k8gb.io Gslb %s/%s. Edit the k8gb.io object going forward.",
			legacy.Namespace,
			legacy.Name,
		)
	} else {
		r.Recorder.Eventf(
			legacy,
			corev1.EventTypeWarning,
			"LegacyIgnored",
			"Legacy Gslb ignored because k8gb.io Gslb %s/%s already exists. Edit the k8gb.io object instead.",
			legacy.Namespace,
			legacy.Name,
		)
	}
	return ctrl.Result{}, nil
}

func hasTrueLabel(labels map[string]string, key string) bool {
	if labels == nil {
		return false
	}
	return labels[key] == "true"
}

func shouldReconcileLegacyMigrationUpdate(e event.TypedUpdateEvent[client.Object]) bool {
	if e.ObjectOld == nil || e.ObjectNew == nil {
		return false
	}

	oldDeleting := e.ObjectOld.GetDeletionTimestamp() != nil
	newDeleting := e.ObjectNew.GetDeletionTimestamp() != nil
	if oldDeleting != newDeleting {
		return true
	}
	if e.ObjectOld.GetGeneration() != e.ObjectNew.GetGeneration() {
		return true
	}

	oldLabels := e.ObjectOld.GetLabels()
	newLabels := e.ObjectNew.GetLabels()
	if hasTrueLabel(oldLabels, migrationRequestLabelKey) != hasTrueLabel(newLabels, migrationRequestLabelKey) {
		return true
	}
	if hasTrueLabel(oldLabels, migrationLabelKey) != hasTrueLabel(newLabels, migrationLabelKey) {
		return true
	}

	return false
}

func (r *LegacyGslbMigrationReconciler) ensureMigrationFinalizer(ctx context.Context, legacy *k8gbv1beta1.Gslb) error {
	if controllerutil.ContainsFinalizer(legacy, legacyMigrationFinalizer) {
		return nil
	}
	patch := client.MergeFrom(legacy.DeepCopy())
	controllerutil.AddFinalizer(legacy, legacyMigrationFinalizer)
	return r.Patch(ctx, legacy, patch)
}

func (r *LegacyGslbMigrationReconciler) removeMigrationFinalizer(ctx context.Context, legacy *k8gbv1beta1.Gslb) error {
	if !controllerutil.ContainsFinalizer(legacy, legacyMigrationFinalizer) {
		return nil
	}
	patch := client.MergeFrom(legacy.DeepCopy())
	controllerutil.RemoveFinalizer(legacy, legacyMigrationFinalizer)
	return r.Patch(ctx, legacy, patch)
}

func (r *LegacyGslbMigrationReconciler) detachLegacyEmbeddedIngressOwner(ctx context.Context, legacy *k8gbv1beta1.Gslb) error {
	if legacy == nil || !hasEmbeddedIngress(legacy.Spec) {
		return nil
	}

	ingress := &netv1.Ingress{}
	if err := r.Get(ctx, types.NamespacedName{Name: legacy.Name, Namespace: legacy.Namespace}, ingress); err != nil {
		if apierrors.IsNotFound(err) {
			return nil
		}
		return err
	}

	filteredOwners := make([]metav1.OwnerReference, 0, len(ingress.OwnerReferences))
	removedLegacyOwner := false
	for _, owner := range ingress.OwnerReferences {
		if owner.UID == legacy.UID {
			removedLegacyOwner = true
			continue
		}
		filteredOwners = append(filteredOwners, owner)
	}
	if !removedLegacyOwner {
		return nil
	}

	patch := client.MergeFrom(ingress.DeepCopy())
	ingress.OwnerReferences = filteredOwners
	return r.Patch(ctx, ingress, patch)
}

func (r *LegacyGslbMigrationReconciler) getCanonicalGslb(
	ctx context.Context,
	legacy *k8gbv1beta1.Gslb,
) (*k8gbv1beta1io.Gslb, error) {
	if legacy == nil {
		return nil, nil
	}

	canonical := &k8gbv1beta1io.Gslb{}
	if err := r.Get(
		ctx,
		types.NamespacedName{Name: legacy.Name, Namespace: legacy.Namespace},
		canonical,
	); err != nil {
		if apierrors.IsNotFound(err) {
			return nil, nil
		}
		return nil, err
	}
	return canonical, nil
}

func (r *LegacyGslbMigrationReconciler) detachLegacyDNSEndpointOwner(
	ctx context.Context,
	legacy *k8gbv1beta1.Gslb,
	canonical *k8gbv1beta1io.Gslb,
) error {
	if legacy == nil {
		return nil
	}

	dnsEndpoint := &externaldnsApi.DNSEndpoint{}
	if err := r.Get(
		ctx,
		types.NamespacedName{Name: legacy.Name, Namespace: legacy.Namespace},
		dnsEndpoint,
	); err != nil {
		if apierrors.IsNotFound(err) {
			return nil
		}
		return err
	}

	filteredOwners := make([]metav1.OwnerReference, 0, len(dnsEndpoint.OwnerReferences))
	removedLegacyOwner := false
	hasCanonicalOwner := false
	hasControllerOwner := false
	for _, owner := range dnsEndpoint.OwnerReferences {
		if owner.UID == legacy.UID {
			removedLegacyOwner = true
			continue
		}
		if canonical != nil && owner.UID == canonical.UID {
			hasCanonicalOwner = true
		}
		if owner.Controller != nil && *owner.Controller {
			hasControllerOwner = true
		}
		filteredOwners = append(filteredOwners, owner)
	}

	addedCanonicalOwner := false
	if canonical != nil && canonical.UID != "" && !hasCanonicalOwner && !hasControllerOwner {
		controllerTrue := true
		blockOwnerDeletion := true
		filteredOwners = append(filteredOwners, metav1.OwnerReference{
			APIVersion:         k8gbv1beta1io.GroupVersion.String(),
			Kind:               "Gslb",
			Name:               canonical.Name,
			UID:                canonical.UID,
			Controller:         &controllerTrue,
			BlockOwnerDeletion: &blockOwnerDeletion,
		})
		addedCanonicalOwner = true
	}

	if !removedLegacyOwner && !addedCanonicalOwner {
		return nil
	}

	patch := client.MergeFrom(dnsEndpoint.DeepCopy())
	dnsEndpoint.OwnerReferences = filteredOwners
	return r.Patch(ctx, dnsEndpoint, patch)
}

func hasEmbeddedIngress(spec k8gbv1beta1.GslbSpec) bool {
	return reflect.DeepEqual(spec.ResourceRef, k8gbv1beta1.ResourceRef{}) &&
		!reflect.DeepEqual(spec.Ingress, k8gbv1beta1.IngressSpec{})
}

func (r *LegacyGslbMigrationReconciler) SetupWithManager(mgr ctrl.Manager) error {
	return ctrl.NewControllerManagedBy(mgr).
		Named("legacy-gslb-migration-controller").
		For(&k8gbv1beta1.Gslb{}).
		WithEventFilter(predicate.Funcs{
			UpdateFunc: shouldReconcileLegacyMigrationUpdate,
		}).
		Complete(r)
}
