func TestCalculateServiceHealth(t *testing.T) {
	testScheme := runtime.NewScheme()
	_ = corev1.AddToScheme(testScheme)
	_ = discov1.AddToScheme(testScheme)
	_ = k8gbv1beta1io.AddToScheme(testScheme)

	tests := []struct {
		name                string
		serverHosts         []string
		serviceHealthPolicy k8gbv1beta1io.ServiceHealthPolicy
		services            map[string][]serviceFixture
		expected            map[string]k8gbv1beta1io.HealthStatus
	}{
		{
			name:        "single server with single healthy service",
			serverHosts: []string{"app.example.com"},
			services: map[string][]serviceFixture{
				"app.example.com": {
					{namespace: "default", name: "svc-a", healthy: true},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"app.example.com": k8gbv1beta1io.Healthy,
			},
		},
		{
			name:        "single server with two healthy services",
			serverHosts: []string{"app.example.com"},
			services: map[string][]serviceFixture{
				"app.example.com": {
					{namespace: "default", name: "svc-a", healthy: true},
					{namespace: "default", name: "svc-b", healthy: true},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"app.example.com": k8gbv1beta1io.Healthy,
			},
		},
		{
			name:                "all policy with two healthy services",
			serverHosts:         []string{"app.example.com"},
			serviceHealthPolicy: k8gbv1beta1io.ServiceHealthPolicyAll,
			services: map[string][]serviceFixture{
				"app.example.com": {
					{namespace: "default", name: "svc-a", healthy: true},
					{namespace: "default", name: "svc-b", healthy: true},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"app.example.com": k8gbv1beta1io.Healthy,
			},
		},
		{
			name:        "default policy with one healthy and one not found service",
			serverHosts: []string{"app.example.com"},
			services: map[string][]serviceFixture{
				"app.example.com": {
					{namespace: "default", name: "svc-a", healthy: true},
					{namespace: "default", name: "svc-b", healthy: true, notFound: true},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"app.example.com": k8gbv1beta1io.Healthy,
			},
		},
		{
			name:                "all policy with one healthy and one not found service",
			serverHosts:         []string{"app.example.com"},
			serviceHealthPolicy: k8gbv1beta1io.ServiceHealthPolicyAll,
			services: map[string][]serviceFixture{
				"app.example.com": {
					{namespace: "default", name: "svc-a", healthy: true},
					{namespace: "default", name: "svc-b", healthy: true, notFound: true},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"app.example.com": k8gbv1beta1io.NotFound,
			},
		},
		{
			name:        "default policy with one healthy and one unhealthy service",
			serverHosts: []string{"app.example.com"},
			services: map[string][]serviceFixture{
				"app.example.com": {
					{namespace: "default", name: "svc-a", healthy: true},
					{namespace: "default", name: "svc-b", healthy: false},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"app.example.com": k8gbv1beta1io.Healthy,
			},
		},
		{
			name:                "all policy with one healthy and one unhealthy service",
			serverHosts:         []string{"app.example.com"},
			serviceHealthPolicy: k8gbv1beta1io.ServiceHealthPolicyAll,
			services: map[string][]serviceFixture{
				"app.example.com": {
					{namespace: "default", name: "svc-a", healthy: true},
					{namespace: "default", name: "svc-b", healthy: false},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"app.example.com": k8gbv1beta1io.Unhealthy,
			},
		},
		{
			name:        "single server with all services not found",
			serverHosts: []string{"app.example.com"},
			services: map[string][]serviceFixture{
				"app.example.com": {
					{namespace: "default", name: "svc-a", healthy: true, notFound: true},
					{namespace: "default", name: "svc-b", healthy: true, notFound: true},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"app.example.com": k8gbv1beta1io.NotFound,
			},
		},
		{
			name:        "single server with all services unhealthy",
			serverHosts: []string{"app.example.com"},
			services: map[string][]serviceFixture{
				"app.example.com": {
					{namespace: "default", name: "svc-a", healthy: false},
					{namespace: "default", name: "svc-b", healthy: false},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"app.example.com": k8gbv1beta1io.Unhealthy,
			},
		},
		{
			name:        "single server with no services",
			serverHosts: []string{"app.example.com"},
			services: map[string][]serviceFixture{
				"app.example.com": {},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"app.example.com": k8gbv1beta1io.NotFound,
			},
		},
		{
			name:        "multiple servers with mixed health",
			serverHosts: []string{"app.example.com", "api.example.com"},
			services: map[string][]serviceFixture{
				"app.example.com": {
					{namespace: "default", name: "svc-a", healthy: true},
				},
				"api.example.com": {
					{namespace: "default", name: "svc-b", healthy: false},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"app.example.com": k8gbv1beta1io.Healthy,
				"api.example.com": k8gbv1beta1io.Unhealthy,
			},
		},
		{
			name:        "Harbor-like ingress with core and portal services - both healthy",
			serverHosts: []string{"harbor.example.com"},
			services: map[string][]serviceFixture{
				"harbor.example.com": {
					{namespace: "harbor", name: "harbor-core", healthy: true},
					{namespace: "harbor", name: "harbor-portal", healthy: true},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"harbor.example.com": k8gbv1beta1io.Healthy,
			},
		},
		{
			name:        "Harbor-like ingress defaults to healthy when one service is healthy",
			serverHosts: []string{"harbor.example.com"},
			services: map[string][]serviceFixture{
				"harbor.example.com": {
					{namespace: "harbor", name: "harbor-core", healthy: true},
					{namespace: "harbor", name: "harbor-portal", healthy: false},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"harbor.example.com": k8gbv1beta1io.Healthy,
			},
		},
		{
			name:                "Harbor-like ingress with all policy and portal down",
			serverHosts:         []string{"harbor.example.com"},
			serviceHealthPolicy: k8gbv1beta1io.ServiceHealthPolicyAll,
			services: map[string][]serviceFixture{
				"harbor.example.com": {
					{namespace: "harbor", name: "harbor-core", healthy: true},
					{namespace: "harbor", name: "harbor-portal", healthy: false},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"harbor.example.com": k8gbv1beta1io.Unhealthy,
			},
		},
		{
			name:                "Harbor-like ingress with all policy and core down",
			serverHosts:         []string{"harbor.example.com"},
			serviceHealthPolicy: k8gbv1beta1io.ServiceHealthPolicyAll,
			services: map[string][]serviceFixture{
				"harbor.example.com": {
					{namespace: "harbor", name: "harbor-core", healthy: false},
					{namespace: "harbor", name: "harbor-portal", healthy: true},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"harbor.example.com": k8gbv1beta1io.Unhealthy,
			},
		},
		{
			name:        "three services all healthy",
			serverHosts: []string{"app.example.com"},
			services: map[string][]serviceFixture{
				"app.example.com": {
					{namespace: "default", name: "svc-a", healthy: true},
					{namespace: "default", name: "svc-b", healthy: true},
					{namespace: "default", name: "svc-c", healthy: true},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"app.example.com": k8gbv1beta1io.Healthy,
			},
		},
		{
			name:                "all policy with three services and one unhealthy",
			serverHosts:         []string{"app.example.com"},
			serviceHealthPolicy: k8gbv1beta1io.ServiceHealthPolicyAll,
			services: map[string][]serviceFixture{
				"app.example.com": {
					{namespace: "default", name: "svc-a", healthy: true},
					{namespace: "default", name: "svc-b", healthy: false},
					{namespace: "default", name: "svc-c", healthy: true},
				},
			},
			expected: map[string]k8gbv1beta1io.HealthStatus{
				"app.example.com": k8gbv1beta1io.Unhealthy,
			},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			ctx := context.Background()
			objs := buildTestObjects(tt.services, tt.name)
			cl := fake.NewClientBuilder().
				WithScheme(testScheme).
				WithObjects(objs...).
				Build()

			gslb := buildTestGslb(tt.serverHosts, tt.services, tt.serviceHealthPolicy)

			result, err := calculateServiceHealth(ctx, cl, gslb)
			require.NoError(t, err)
			assert.Equal(t, tt.expected, result)
		})
	}
}

type serviceFixture struct {
	namespace string
	name      string
	healthy   bool
	notFound  bool
}

func buildTestObjects(services map[string][]serviceFixture, testName string) []client.Object {
	var objs []client.Object
	for _, fixtures := range services {
		for _, fixture := range fixtures {
			if fixture.notFound {
				continue
			}
			svc := &corev1.Service{
				ObjectMeta: metav1.ObjectMeta{
					Name:      fixture.name,
					Namespace: fixture.namespace,
				},
			}
			objs = append(objs, svc)

			epsName := fixture.name
			if testName != "" {
				epsName = testName + "-" + fixture.name
			}

			if fixture.healthy {
				eps := &discov1.EndpointSlice{
					ObjectMeta: metav1.ObjectMeta{
						Name:      epsName,
						Namespace: fixture.namespace,
						Labels: map[string]string{
							discov1.LabelServiceName: fixture.name,
						},
					},
					Endpoints: []discov1.Endpoint{
						{
							Addresses: []string{"10.0.0.1"},
							Conditions: discov1.EndpointConditions{
								Ready: ptr(true),
							},
						},
					},
				}
				objs = append(objs, eps)
			} else {
				eps := &discov1.EndpointSlice{
					ObjectMeta: metav1.ObjectMeta{
						Name:      epsName,
						Namespace: fixture.namespace,
						Labels: map[string]string{
							discov1.LabelServiceName: fixture.name,
						},
					},
					Endpoints: []discov1.Endpoint{},
				}
				objs = append(objs, eps)
			}
		}
	}
	return objs
}

func buildTestGslb(
	hosts []string,
	services map[string][]serviceFixture,
	serviceHealthPolicy k8gbv1beta1io.ServiceHealthPolicy,
) *k8gbv1beta1io.Gslb {
	servers := make([]*k8gbv1beta1io.Server, 0, len(hosts))
	for _, host := range hosts {
		fixtures := services[host]
		serviceRefs := make([]*k8gbv1beta1io.NamespacedName, 0, len(fixtures))
		for _, fixture := range fixtures {
			serviceRefs = append(serviceRefs, &k8gbv1beta1io.NamespacedName{
				Namespace: fixture.namespace,
				Name:      fixture.name,
			})
		}
		servers = append(servers, &k8gbv1beta1io.Server{
			Host:     host,
			Services: serviceRefs,
		})
	}
	return &k8gbv1beta1io.Gslb{
		ObjectMeta: metav1.ObjectMeta{
			Name:      "test-gslb",
			Namespace: "default",
		},
		Spec: k8gbv1beta1io.GslbSpec{
			ServiceHealthPolicy: serviceHealthPolicy,
		},
		Status: k8gbv1beta1io.GslbStatus{
			Servers: servers,
		},
	}
}

func ptr[T any](v T) *T { return &v }
