func TestLegacyMigrationConvertsEmbeddedIngressToReference(t *testing.T) {
	className := testIngressClassName
	legacyUID := types.UID("legacy-uid")
	keepUID := types.UID("other-uid")
	keepController := true
	legacy := &k8gbv1beta1.Gslb{
		ObjectMeta: metav1.ObjectMeta{
			Name:      "demo",
			Namespace: "default",
			UID:       legacyUID,
			Labels: map[string]string{
				migrationRequestLabelKey: "true",
			},
		},
		Spec: k8gbv1beta1.GslbSpec{
			Ingress:  k8gbv1beta1.IngressSpec{IngressClassName: &className},
			Strategy: k8gbv1beta1.Strategy{Type: "roundRobin", DNSTtlSeconds: 30},
		},
	}
	ingress := &netv1.Ingress{
		ObjectMeta: metav1.ObjectMeta{
			Name:      "demo",
			Namespace: "default",
			OwnerReferences: []metav1.OwnerReference{
				{UID: legacyUID, APIVersion: k8gbv1beta1.GroupVersion.String(), Kind: "Gslb", Name: "demo"},
				{UID: keepUID, APIVersion: "v1", Kind: "ConfigMap", Name: "keep-me", Controller: &keepController},
			},
		},
	}
	r, cl, _ := newLegacyMigrationReconciler(t, legacy, ingress)

	_, err := r.Reconcile(context.Background(), ctrl.Request{NamespacedName: types.NamespacedName{Name: "demo", Namespace: "default"}})
	require.NoError(t, err)

	canonical := &k8gbv1beta1io.Gslb{}
	err = cl.Get(context.Background(), types.NamespacedName{Name: "demo", Namespace: "default"}, canonical)
	require.NoError(t, err)
	require.Equal(t, "networking.k8s.io/v1", canonical.Spec.ResourceRef.APIVersion)
	require.Equal(t, "Ingress", canonical.Spec.ResourceRef.Kind)
	require.Equal(t, "demo", canonical.Spec.ResourceRef.Name)
	require.Equal(t, "default", canonical.Spec.ResourceRef.Namespace)
	require.Equal(t, k8gbv1beta1io.IngressSpec{}, canonical.Spec.Ingress)

	updatedIngress := &netv1.Ingress{}
	err = cl.Get(context.Background(), types.NamespacedName{Name: "demo", Namespace: "default"}, updatedIngress)
	require.NoError(t, err)
	require.Len(t, updatedIngress.OwnerReferences, 1)
	require.Equal(t, keepUID, updatedIngress.OwnerReferences[0].UID)
}

