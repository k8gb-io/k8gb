func TestHasEmbeddedIngress(t *testing.T) {
	className := testIngressClassName
	spec := k8gbv1beta1.GslbSpec{
		Ingress: k8gbv1beta1.IngressSpec{IngressClassName: &className},
	}
	require.True(t, hasEmbeddedIngress(spec))
	require.False(t, hasEmbeddedIngress(k8gbv1beta1.GslbSpec{}))
	spec.ResourceRef = k8gbv1beta1.ResourceRef{ObjectReference: corev1.ObjectReference{Name: "demo"}}
	require.False(t, hasEmbeddedIngress(spec))
}

func newLegacyMigrationReconciler(t *testing.T, objs ...client.Object) (*LegacyGslbMigrationReconciler, client.Client, *record.FakeRecorder) {
	t.Helper()

	s := runtime.NewScheme()
	require.NoError(t, clientgoscheme.AddToScheme(s))
	require.NoError(t, k8gbv1beta1.AddToScheme(s))
	require.NoError(t, k8gbv1beta1io.AddToScheme(s))
	require.NoError(t, externaldnsApi.AddToScheme(s))

	cl := fake.NewClientBuilder().WithScheme(s).WithObjects(objs...).Build()
	recorder := record.NewFakeRecorder(10)
	r := &LegacyGslbMigrationReconciler{Client: cl, Recorder: recorder}
	return r, cl, recorder
}
