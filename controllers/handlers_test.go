import (
	"context"
	"github.com/k8gb-io/k8gb/controllers/mocks"
	"github.com/k8gb-io/k8gb/controllers/utils"
	"github.com/stretchr/testify/assert"
	"go.uber.org/mock/gomock"
	netv1 "k8s.io/api/networking/v1"
	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"
	"testing"
)

func TestIngressHandler(t *testing.T) {
	const def = "default"
	var spec = netv1.IngressSpec{
		IngressClassName: utils.Ptr("nginx"),
		Rules: []netv1.IngressRule{
			{
				Host: "app.claud.example.com",
				IngressRuleValue: netv1.IngressRuleValue{
					HTTP: &netv1.HTTPIngressRuleValue{
						Paths: []netv1.HTTPIngressPath{
							{
								Path:     "/",
								PathType: (*netv1.PathType)(utils.Ptr("Prefix")),
								Backend: netv1.IngressBackend{
									Service: &netv1.IngressServiceBackend{
										Name: def,
										Port: netv1.ServiceBackendPort{
											Name:   "http",
											Number: 8080,
										},
									},
								},
							},
						},
					},
				},
			},
		},
	}

	var tests = []struct {
		name      string
		ing       *netv1.Ingress
		getClient func(*gomock.Controller) *mocks.MockClient
	}{
		{
			name: "Ingress has not k8gb annotation",
			ing: &netv1.Ingress{
				ObjectMeta: metav1.ObjectMeta{
					Name:        def,
					Namespace:   def,
					Annotations: map[string]string{def: def},
				},
				Spec: spec,
			},
			getClient: mocks.NewMockClient,
		},
		{
			name: "Ingress already has gslb reference",
			ing: &netv1.Ingress{
				ObjectMeta: metav1.ObjectMeta{
					Name:        def,
					Namespace:   def,
					Annotations: map[string]string{def: def, strategyAnnotation: "roundRobin"},
					OwnerReferences: []metav1.OwnerReference{
						{
							Name: def,
							Kind: "Gslb",
						},
					},
				},
				Spec: spec,
			},
			getClient: mocks.NewMockClient,
		},
	}
	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {
			ctrl := gomock.NewController(t)
			defer ctrl.Finish()
			client := test.getClient(ctrl)
			handler := NewIngressHandler(client, nil)
			result := handler.Handle(context.TODO(), test.ing)
			assert.Nil(t, result)
		})
	}
}
