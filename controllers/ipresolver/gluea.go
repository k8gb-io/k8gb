import "github.com/k8gb-io/k8gb/controllers/utils"

type GlueAInfo struct {
	GeoTag  string
	IP      string
	Cluster string
	Err     error
	Status  utils.DNSQueryStatus
	IsLocal bool
}

type ClusterGlueAResults []*GlueAInfo

func (r ClusterGlueAResults) LocalClusterError() error {
	for _, glueAInfo := range r.LocalClusterRecords() {
		if glueAInfo.Err != nil {
			return glueAInfo.Err
		}
	}
	return nil
}

func (r ClusterGlueAResults) LocalClusterRecords() ClusterGlueAResults {
	l := ClusterGlueAResults{}
	for _, glueAInfo := range r {
		if glueAInfo.IsLocal {
			l = append(l, glueAInfo)
		}
	}
	return l
}

func (r ClusterGlueAResults) FilterResolvedRecords() ClusterGlueAResults {
	l := ClusterGlueAResults{}
	for _, glueAInfo := range r {
		if glueAInfo.Status == utils.DNSQueryStatusResolved {
			l = append(l, glueAInfo)
		}
	}
	return l
}
