import (
	"context"
	"fmt"

	"github.com/k8gb-io/k8gb/controllers/providers/assistant"
	"github.com/k8gb-io/k8gb/controllers/resolver"
	"github.com/k8gb-io/k8gb/controllers/utils"

	corev1 "k8s.io/api/core/v1"
	netv1 "k8s.io/api/networking/v1"
	"sigs.k8s.io/controller-runtime/pkg/client"
)

type Resolved struct {
	source corev1.ServiceType
	svc    *corev1.Service
	ing    *netv1.Ingress
	IPs    []string
	Name   string
}

type GlueAInfo struct {
	GeoTag  string
	IP      string
	Cluster string
}

type Resolver interface {
	GetExposedIPs(ctx context.Context) (*Resolved, error)

	GetGlueAInfo(ctx context.Context, loadBalancedZone, parentZone string) ([]*GlueAInfo, error)
}

type ResolverImpl struct {
	config       *resolver.Config
	client       client.Client
	queryService utils.DNSQueryService
}

func NewResolver(config *resolver.Config, cl client.Client, queryService utils.DNSQueryService) *ResolverImpl {
	return &ResolverImpl{
		config:       config,
		client:       cl,
		queryService: queryService,
	}
}

func (b *ResolverImpl) GetExposedIPs(ctx context.Context) (*Resolved, error) {
	bootstrap, err := readIPs(ctx, b.client, b.config)
	if err != nil {
		return nil, err
	}
	if len(bootstrap.IPs) == 0 {
		return nil, fmt.Errorf("no IP addresses found")
	}
	return bootstrap, nil
}

// GetGlueAInfo returns a list of GlueAInfo records containing NS names and their
// corresponding IP addresses for both external and local clusters.
// For external clusters, it resolves NS names via DNS queries and extracts A records.
// For the local cluster, it uses IPs obtained from exposed services (e.g. LoadBalancer)
func (b *ResolverImpl) GetGlueAInfo(ctx context.Context, loadBalancedZone, parentZone string) ([]*GlueAInfo, error) {
	gainfo := make([]*GlueAInfo, 0)
	extClusterNSNames := b.config.GetExtClusterNSNames(loadBalancedZone, parentZone)
	clusterNSName := b.config.GetNsName(loadBalancedZone, parentZone)
	for tag, cluster := range extClusterNSNames {
		// Use edgeDNSServer for resolution of NS names and fallback to local nameservers
		glueA, err := b.queryService.Query(cluster, b.config.ParentZoneDNSServers)
		if err != nil {
			return nil, err
		}
		glueARecords := b.queryService.ExtractARecords(glueA)
		if len(glueARecords) > 0 {
			for _, glueARecord := range glueARecords {
				gainfo = append(gainfo, &GlueAInfo{GeoTag: tag, IP: glueARecord, Cluster: cluster})
			}
			continue
		}
		gainfo = append(gainfo, &GlueAInfo{GeoTag: tag, IP: "", Cluster: cluster})
	}
	localIPs, err := b.GetExposedIPs(ctx)
	if err != nil {
		return nil, err
	}
	for _, ip := range localIPs.IPs {
		gainfo = append(gainfo, &GlueAInfo{GeoTag: b.config.ClusterGeoTag, IP: ip, Cluster: clusterNSName})
	}
	return gainfo, nil
}

func (b *Resolved) HasIngress() bool {
	return b.source != corev1.ServiceTypeLoadBalancer
}

func (b *Resolved) String() string {
	if b.HasIngress() {
		return fmt.Sprintf("Ingress %s/%s %s", b.ing.Namespace, b.ing.Name, b.IPs)
	}
	return fmt.Sprintf("IPResolverService type LoadBalancer %s/%s %s", b.svc.Namespace, b.svc.Name, b.IPs)
}

func readIPs(ctx context.Context, cl client.Client, config *resolver.Config) (*Resolved, error) {
	var err error
	boot := &Resolved{source: config.CoreDNSServiceType}
	if boot.HasIngress() {
		ingresAssistant := assistant.NewIngressAssistant(ctx, cl)
		boot.IPs, boot.ing, err = ingresAssistant.GetExposedIPs()
		return boot, err
	}
	coreDNSAssistant := assistant.NewCoreDNSServiceAssistant(ctx, cl, *config)
	boot.IPs, boot.svc, err = coreDNSAssistant.GetExposedIPs()
	return boot, err
}
