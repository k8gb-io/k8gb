func TestResolveNSNames(t *testing.T) {

	coreDNSService := corev1.Service{
		ObjectMeta: metav1.ObjectMeta{
			Name:      "test",
			Namespace: "test",
		},
		Spec: corev1.ServiceSpec{
			Type: corev1.ServiceTypeLoadBalancer,
		},
		Status: corev1.ServiceStatus{
			LoadBalancer: corev1.LoadBalancerStatus{
				Ingress: []corev1.LoadBalancerIngress{},
			},
		},
	}

	tests := []struct {
		name             string
		expectedError    bool
		config           *resolver.Config
		loadBalancedZone string
		parentZone       string
		expectedResult   []*GlueAInfo
		arrange          func(*mocks.MockDNSQueryService, *mocks.MockClient)
	}{
		{
			name: "provide NS records",
			config: &resolver.Config{
				CoreDNSServiceType:    corev1.ServiceTypeLoadBalancer,
				ClusterGeoTag:         "us",
				ExtClustersGeoTagsRaw: []string{"eu", "za"},
			},
			loadBalancedZone: "cloud.example.com",
			parentZone:       "example.com",
			expectedResult: []*GlueAInfo{
				{IP: "172.18.0.1", Cluster: "gslb-ns-eu-cloud.example.com", GeoTag: "eu"},
				{IP: "172.18.0.2", Cluster: "gslb-ns-eu-cloud.example.com", GeoTag: "eu"},
				{IP: "172.20.0.1", Cluster: "gslb-ns-za-cloud.example.com", GeoTag: "za"},
				{IP: "172.20.0.2", Cluster: "gslb-ns-za-cloud.example.com", GeoTag: "za"},
				{IP: "172.10.10.10", Cluster: "gslb-ns-us-cloud.example.com", GeoTag: "us"},
				{IP: "172.10.10.11", Cluster: "gslb-ns-us-cloud.example.com", GeoTag: "us"},
			},
			arrange: func(qs *mocks.MockDNSQueryService, cl *mocks.MockClient) {
				qs.EXPECT().Query(gomock.Any(), gomock.Any()).Return(&dns.Msg{}, nil).AnyTimes()
				qs.EXPECT().ExtractARecords(gomock.Any()).Return([]string{"172.18.0.1", "172.18.0.2"}).Times(1)
				qs.EXPECT().ExtractARecords(gomock.Any()).Return([]string{"172.20.0.1", "172.20.0.2"}).Times(1)
				svc := coreDNSService.DeepCopy()
				svc.Status.LoadBalancer.Ingress = []corev1.LoadBalancerIngress{{IP: "172.10.10.10"}, {IP: "172.10.10.11"}}
				cl.EXPECT().List(gomock.Any(), gomock.Any(), gomock.Any()).
					DoAndReturn(func(_ context.Context, obj client.ObjectList, _ ...client.ListOption) error {
						serviceList := obj.(*corev1.ServiceList)
						serviceList.Items = []corev1.Service{*svc}
						return nil
					}).Times(1)
			},
		},
		{
			name: "external NS records missing",
			config: &resolver.Config{
				CoreDNSServiceType:    corev1.ServiceTypeLoadBalancer,
				ClusterGeoTag:         "us",
				ExtClustersGeoTagsRaw: []string{"eu", "za"},
			},
			loadBalancedZone: "cloud.example.com",
			parentZone:       "example.com",
			expectedResult: []*GlueAInfo{
				{IP: "", Cluster: "gslb-ns-eu-cloud.example.com", GeoTag: "eu"},
				{IP: "", Cluster: "gslb-ns-eu-cloud.example.com", GeoTag: "eu"},
				{IP: "", Cluster: "gslb-ns-za-cloud.example.com", GeoTag: "za"},
				{IP: "", Cluster: "gslb-ns-za-cloud.example.com", GeoTag: "za"},
				{IP: "172.10.10.10", Cluster: "gslb-ns-us-cloud.example.com", GeoTag: "us"},
				{IP: "172.10.10.11", Cluster: "gslb-ns-us-cloud.example.com", GeoTag: "us"},
			},
			arrange: func(qs *mocks.MockDNSQueryService, cl *mocks.MockClient) {
				qs.EXPECT().Query(gomock.Any(), gomock.Any()).Return(&dns.Msg{}, nil).AnyTimes()
				qs.EXPECT().ExtractARecords(gomock.Any()).Return([]string{}).Times(1)
				qs.EXPECT().ExtractARecords(gomock.Any()).Return([]string{}).Times(1)
				svc := coreDNSService.DeepCopy()
				svc.Status.LoadBalancer.Ingress = []corev1.LoadBalancerIngress{{IP: "172.10.10.10"}, {IP: "172.10.10.11"}}
				cl.EXPECT().List(gomock.Any(), gomock.Any(), gomock.Any()).
					DoAndReturn(func(_ context.Context, obj client.ObjectList, _ ...client.ListOption) error {
						serviceList := obj.(*corev1.ServiceList)
						serviceList.Items = []corev1.Service{*svc}
						return nil
					}).Times(1)
			},
		},
		{
			name: "external NS records error",
			config: &resolver.Config{
				CoreDNSServiceType:    corev1.ServiceTypeLoadBalancer,
				ClusterGeoTag:         "us",
				ExtClustersGeoTagsRaw: []string{"eu", "za"},
			},
			loadBalancedZone: "cloud.example.com",
			parentZone:       "example.com",
			expectedResult:   nil,
			arrange: func(qs *mocks.MockDNSQueryService, cl *mocks.MockClient) {
				qs.EXPECT().Query(gomock.Any(), gomock.Any()).Return(nil, fmt.Errorf("dns error")).AnyTimes()
				svc := coreDNSService.DeepCopy()
				svc.Status.LoadBalancer.Ingress = []corev1.LoadBalancerIngress{{IP: "172.10.10.10"}, {IP: "172.10.10.11"}}
				cl.EXPECT().List(gomock.Any(), gomock.Any(), gomock.Any()).
					DoAndReturn(func(_ context.Context, obj client.ObjectList, _ ...client.ListOption) error {
						serviceList := obj.(*corev1.ServiceList)
						serviceList.Items = []corev1.Service{*svc}
						return nil
					}).AnyTimes()
			},
			expectedError: true,
		},
	}
	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {

			ctrl := gomock.NewController(t)
			defer ctrl.Finish()
			cl := mocks.NewMockClient(ctrl)
			qs := mocks.NewMockDNSQueryService(ctrl)
			test.arrange(qs, cl)

			info, err := NewResolver(test.config, cl, qs).GetGlueAInfo(context.TODO(), test.loadBalancedZone, test.parentZone)
			if test.expectedError {
				assert.Nil(t, info)
				assert.Error(t, err)
				return
			}
			assert.NoError(t, err)
			for i, glueAInfo := range test.expectedResult {
				assert.Equal(t, test.expectedResult[i].IP, glueAInfo.IP)
				assert.Equal(t, test.expectedResult[i].Cluster, glueAInfo.Cluster)
				assert.Equal(t, test.expectedResult[i].GeoTag, glueAInfo.GeoTag)
			}
		})
	}
}

func TestResolveLocalIPs(t *testing.T) {

	objectMeta := metav1.ObjectMeta{
		Name:      "k8gb-coredns",
		Namespace: "k8gb",
	}
	ctx := context.TODO()
	tests := []struct {
		name          string
		config        *resolver.Config
		expectedIPs   []string
		setupClient   func(_ []string) client.Client
		expectedError bool
		hasIngress    bool
	}{
		{
			name:          "CoreDNSService missing",
			expectedError: true,
			hasIngress:    false,
			config: &resolver.Config{
				K8gbNamespace: "k8gb",
			},
			setupClient: func(_ []string) client.Client {
				serviceList := &corev1.ServiceList{
					ListMeta: metav1.ListMeta{},
					Items:    []corev1.Service{},
				}
				cl := fake.NewClientBuilder().WithScheme(scheme.Scheme).WithLists(serviceList).Build()
				_ = cl.List(ctx, serviceList, &client.ListOptions{})
				return cl
			},
		},

		{
			name:          "CoreDNSService IS LoadBalancer",
			expectedError: false,
			expectedIPs:   []string{"10.0.0.1", "10.0.0.2"},
			hasIngress:    false,
			config: &resolver.Config{
				K8gbNamespace:      "k8gb",
				CoreDNSServiceType: corev1.ServiceTypeLoadBalancer,
			},
			setupClient: func(expectedIPs []string) client.Client {
				coreDNSService := &corev1.Service{
					ObjectMeta: metav1.ObjectMeta{
						Name:      objectMeta.Name,
						Namespace: objectMeta.Namespace,
						Labels: map[string]string{
							"app.kubernetes.io/name": "coredns",
						},
					},
					Spec: corev1.ServiceSpec{
						Type: corev1.ServiceTypeLoadBalancer,
					},
					Status: corev1.ServiceStatus{
						LoadBalancer: corev1.LoadBalancerStatus{
							Ingress: []corev1.LoadBalancerIngress{
								{
									IP: expectedIPs[0],
								},
								{
									IP: expectedIPs[1],
								},
							},
						},
					},
				}
				cl := fake.NewClientBuilder().WithScheme(scheme.Scheme).WithObjects(coreDNSService).Build()
				return cl
			},
		},

		{
			name:          "CoreDNSService IS NOT LoadBalancer",
			expectedError: false,
			expectedIPs:   []string{"10.0.0.1", "10.0.0.2"},
			hasIngress:    true,
			config: &resolver.Config{
				K8gbNamespace:      "k8gb",
				CoreDNSServiceType: corev1.ServiceTypeClusterIP,
			},
			setupClient: func(expectedIPs []string) client.Client {
				coreDNSService := &corev1.Service{
					ObjectMeta: metav1.ObjectMeta{
						Name:      objectMeta.Name,
						Namespace: objectMeta.Namespace,
						Labels: map[string]string{
							assistant.CoreDNSServiceLabelName: assistant.CoreDNSServiceLabelValue,
						},
					},
					Spec: corev1.ServiceSpec{
						Type: corev1.ServiceTypeClusterIP,
					},
					Status: corev1.ServiceStatus{
						LoadBalancer: corev1.LoadBalancerStatus{
							Ingress: []corev1.LoadBalancerIngress{
								{
									IP: expectedIPs[0],
								},
								{
									IP: expectedIPs[1],
								},
							},
						},
					},
				}
				ing := &netv1.Ingress{
					ObjectMeta: metav1.ObjectMeta{
						Name:      "coredns-ingress",
						Namespace: "k8gb",
						Labels: map[string]string{
							assistant.IngressLabelName: assistant.IngressLabelValue,
						},
					},
					Status: netv1.IngressStatus{
						LoadBalancer: netv1.IngressLoadBalancerStatus{
							Ingress: []netv1.IngressLoadBalancerIngress{
								{
									IP: expectedIPs[0],
								},
								{
									IP: expectedIPs[1],
								},
							},
						},
					},
				}
				cl := fake.NewClientBuilder().WithScheme(scheme.Scheme).WithObjects(coreDNSService).WithObjects(ing).Build()
				return cl
			},
		},

		{
			name:          "CoreDNSService IS NOT LoadBalancer and wrong Ingress Annotation",
			expectedError: true,
			expectedIPs:   []string{"10.0.0.1", "10.0.0.2"},
			hasIngress:    true,
			config: &resolver.Config{
				K8gbNamespace:      "k8gb",
				CoreDNSServiceType: corev1.ServiceTypeClusterIP,
			},
			setupClient: func(expectedIPs []string) client.Client {
				coreDNSService := &corev1.Service{
					ObjectMeta: metav1.ObjectMeta{
						Name:      objectMeta.Name,
						Namespace: objectMeta.Namespace,
						Labels: map[string]string{
							assistant.CoreDNSServiceLabelName: assistant.CoreDNSServiceLabelValue,
						},
					},
					Spec: corev1.ServiceSpec{
						Type: corev1.ServiceTypeClusterIP,
					},
					Status: corev1.ServiceStatus{
						LoadBalancer: corev1.LoadBalancerStatus{
							Ingress: []corev1.LoadBalancerIngress{
								{
									IP: expectedIPs[0],
								},
								{
									IP: expectedIPs[1],
								},
							},
						},
					},
				}
				ing := &netv1.Ingress{
					ObjectMeta: metav1.ObjectMeta{
						Name:      "coredns-ingress",
						Namespace: "k8gb",
					},
					Status: netv1.IngressStatus{
						LoadBalancer: netv1.IngressLoadBalancerStatus{
							Ingress: []netv1.IngressLoadBalancerIngress{
								{
									IP: expectedIPs[0],
								},
								{
									IP: expectedIPs[1],
								},
							},
						},
					},
				}
				cl := fake.NewClientBuilder().WithScheme(scheme.Scheme).WithObjects(coreDNSService).WithObjects(ing).Build()
				return cl
			},
		},
	}
	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {
			cl := test.setupClient(test.expectedIPs)
			bootstrap, err := NewResolver(test.config, cl, nil).GetExposedIPs(ctx)
			if test.expectedError {
				assert.Nil(t, bootstrap)
				assert.Error(t, err)
				return
			}
			assert.Equal(t, test.expectedIPs, bootstrap.IPs)
			assert.Equal(t, test.hasIngress, bootstrap.HasIngress())
		})
	}
}
