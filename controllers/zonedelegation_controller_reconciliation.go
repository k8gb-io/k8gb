import (
	"context"

	"github.com/k8gb-io/k8gb/controllers/bootstrap"

	"github.com/k8gb-io/k8gb/controllers/providers/dns"
	"github.com/k8gb-io/k8gb/controllers/resolver"
	"github.com/k8gb-io/k8gb/controllers/utils"
	"github.com/k8gb-io/k8gb/controllers/zones"
	"k8s.io/apimachinery/pkg/runtime"
	ctrl "sigs.k8s.io/controller-runtime"
	"sigs.k8s.io/controller-runtime/pkg/client"
)

type ZoneDelegationReconciler struct {
	client.Client
	Config           *resolver.Config
	Scheme           *runtime.Scheme
	DNSProvider      dns.Provider
	ZoneService      zones.ZoneDelegation
	BootstrapService *bootstrap.BoundIPsServiceImpl
}

func (r *ZoneDelegationReconciler) Reconcile(ctx context.Context, req ctrl.Request) (ctrl.Result, error) {
	result := utils.NewReconcileResultHandler(0)

	if !r.ZoneService.HasAvailableIPs(ctx) {
		logger.Debug().Msg("Waiting for available IPs. Skipping ZoneDelegation reconciliation")
		return result.Requeue()
	}

	zone, err := r.ZoneService.Get(ctx, req.NamespacedName)
	if err != nil {
		logger.Err(err).Msg("Error processing ZoneDelegation")
		return result.RequeueError(err)
	}

	if zone.DeletionTimestamp != nil {
		err := r.finalize(ctx, client.ObjectKey{Name: zone.Name, Namespace: zone.Namespace})
		if err != nil {
			return ctrl.Result{}, err
		}
		return result.Stop()
	}

	err = r.ZoneService.UpdateStatus(ctx, zone)
	if err != nil {
		logger.Err(err).Str("Name", zone.Name).Msg("Error updating zone delegation")
		return result.RequeueError(err)
	}

	return result.Stop()
}

func (r *ZoneDelegationReconciler) finalize(ctx context.Context, key client.ObjectKey) error {
	zoneInfo, err := r.ZoneService.Get(ctx, key)
	if err != nil {
		return err
	}
	zoneDetail, err := zones.NewZoneDelegationWrapper(zoneInfo, r.Config, r.BootstrapService).GetDetail(ctx)
	if err != nil {
		return err
	}
	return r.DNSProvider.Finalize(zoneDetail, true)
}
