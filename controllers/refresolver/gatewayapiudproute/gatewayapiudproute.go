var log = logging.Logger()

type ReferenceResolver struct {
	route   *UDPRouteAdapter
	gateway *gatewayapiv1.Gateway
}

// NewReferenceResolver creates a new reference resolver capable of understanding `networking.gateway.api/v1` resources
func NewReferenceResolver(gslb *k8gbv1beta1io.Gslb, k8sClient client.Client) (*ReferenceResolver, error) {
	udpRouteList, err := getGslbUDPRouteRef(gslb, k8sClient)
	if err != nil {
		return nil, err
	}

	if len(udpRouteList) != 1 {
		return nil, fmt.Errorf("exactly 1 UDPRoute resource expected but %d were found", len(udpRouteList))
	}
	udpRoute := udpRouteList[0]

	route := NewUDPRouteAdapter(&udpRoute, gslb.Annotations[utils.HostnameAnnotation])

	gateway, err := gatewayapi.GetGateway(route, k8sClient)
	if err != nil {
		return nil, err
	}

	return &ReferenceResolver{
		route:   route,
		gateway: gateway,
	}, nil
}

// getGslbUDPRouteRef resolves a Gateway API UDPRoute resource referenced by the Gslb spec
func getGslbUDPRouteRef(gslb *k8gbv1beta1io.Gslb, k8sClient client.Client) ([]gatewayapiv1alpha2.UDPRoute, error) {
	query, err := queryopts.Get(gslb.Spec.ResourceRef, gslb.Namespace)
	if err != nil {
		return nil, err
	}

	switch query.Mode {
	case queryopts.QueryModeGet:
		var udpRoute = gatewayapiv1alpha2.UDPRoute{}
		err = k8sClient.Get(context.TODO(), *query.GetKey, &udpRoute)
		if err != nil {
			if errors.IsNotFound(err) {
				log.Info().
					Str("gslb", gslb.Name).
					Str("namespace", gslb.Namespace).
					Msg("Can't find referenced UDPRoute resource")
			}
			return nil, err
		}
		return []gatewayapiv1alpha2.UDPRoute{udpRoute}, nil

	case queryopts.QueryModeList:
		udpRouteList := &gatewayapiv1alpha2.UDPRouteList{}
		err = k8sClient.List(context.TODO(), udpRouteList, query.ListOpts...)
		if err != nil {
			if errors.IsNotFound(err) {
				log.Info().
					Str("gslb", gslb.Name).
					Str("namespace", gslb.Namespace).
					Msg("Can't find referenced UDPRoute resource")
			}
			return nil, err
		}
		return udpRouteList.Items, nil
	}
	return nil, fmt.Errorf("unknown query mode %v", query.Mode)
}

// GetServers retrieves the GSLB server configuration from the UDPRoute resource
func (rr *ReferenceResolver) GetServers() ([]*k8gbv1beta1io.Server, error) {
	return gatewayapi.GetServersFromRoute(rr.route)
}

// GetGslbExposedIPs retrieves the load balancer IP address of the GSLB
func (rr *ReferenceResolver) GetGslbExposedIPs(gslbAnnotations map[string]string, parentZoneDNSServers utils.DNSList) ([]string, error) {
	return gatewayapi.GetGslbExposedIPs(rr.gateway, gslbAnnotations, parentZoneDNSServers)
}
