import (
	"testing"

	k8gbv1beta1 "github.com/k8gb-io/k8gb/api/v1beta1"
	"github.com/k8gb-io/k8gb/controllers/utils"
	"github.com/stretchr/testify/assert"
)

func TestGetServers(t *testing.T) {
	var tests = []struct {
		name            string
		udpRouteFile    string
		gslbAnnotations map[string]string
		expectedServers []*k8gbv1beta1.Server
		expectError     bool
	}{
		{
			name:            "missing hostname annotation",
			udpRouteFile:    "../testdata/gatewayapi_udproute.yaml",
			gslbAnnotations: map[string]string{},
			expectedServers: nil,
			expectError:     true,
		},
		{
			name:         "empty hostname annotation",
			udpRouteFile: "../testdata/gatewayapi_udproute.yaml",
			gslbAnnotations: map[string]string{
				utils.HostnameAnnotation: "",
			},
			expectedServers: nil,
			expectError:     true,
		},
		{
			name:         "valid UDPRoute with hostname annotation; backend without namespace specified",
			udpRouteFile: "../testdata/gatewayapi_udproute.yaml",
			gslbAnnotations: map[string]string{
				utils.HostnameAnnotation: "gatewayapi-udproute.cloud.example.com",
			},
			expectedServers: []*k8gbv1beta1.Server{
				{
					Host: "gatewayapi-udproute.cloud.example.com",
					Services: []*k8gbv1beta1.NamespacedName{
						{
							Name:      "gatewayapi-udproute-service",
							Namespace: "test-gslb",
						},
					},
				},
			},
		},
		{
			name:         "valid UDPRoute with hostname annotation; backend with namespace specified",
			udpRouteFile: "./testdata/gatewayapi_udproute_backend_with_namespace.yaml",
			gslbAnnotations: map[string]string{
				utils.HostnameAnnotation: "gatewayapi-udproute.cloud.example.com",
			},
			expectedServers: []*k8gbv1beta1.Server{
				{
					Host: "gatewayapi-udproute.cloud.example.com",
					Services: []*k8gbv1beta1.NamespacedName{
						{
							Name:      "gatewayapi-udproute-service",
							Namespace: "test-backend",
						},
					},
				},
			},
		},
		{
			name:         "multiple backendRefs",
			udpRouteFile: "./testdata/gatewayapi_udproute_multiple_backendrefs.yaml",
			gslbAnnotations: map[string]string{
				utils.HostnameAnnotation: "gatewayapi-udproute.cloud.example.com",
			},
			expectedServers: []*k8gbv1beta1.Server{
				{
					Host: "gatewayapi-udproute.cloud.example.com",
					Services: []*k8gbv1beta1.NamespacedName{
						{
							Name:      "gatewayapi-udproute-service1",
							Namespace: "test-gslb",
						},
						{
							Name:      "gatewayapi-udproute-service2",
							Namespace: "test-gslb",
						},
					},
				},
			},
		},
	}
	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {
			// arrange
			udpRoute := utils.FileToGatewayApiUdpRoute(test.udpRouteFile)
			resolver := ReferenceResolver{
				route: NewUDPRouteAdapter(udpRoute, test.gslbAnnotations[utils.HostnameAnnotation]),
			}

			// act
			servers, err := resolver.GetServers()

			// assert
			if test.expectError {
				assert.Error(t, err)
				assert.Nil(t, servers)
			} else {
				assert.NoError(t, err)
				assert.Equal(t, test.expectedServers, servers)
			}
		})
	}
}
