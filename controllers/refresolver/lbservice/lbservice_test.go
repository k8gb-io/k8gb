const (
	dnsZone = "cloud.example.com"
)

func TestGetServers(t *testing.T) {
	var tests = []struct {
		name            string
		svcFile         string
		expectedServers []*k8gbv1beta1.Server
	}{
		{
			name:    "single server",
			svcFile: "../testdata/lb_service.yaml",
			expectedServers: []*k8gbv1beta1.Server{
				{
					Host: "lb-service.cloud.example.com",
					Services: []*k8gbv1beta1.NamespacedName{
						{
							Name:      "lb-service",
							Namespace: "test-gslb",
						},
					},
				},
			},
		},
	}
	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {
			// arrange
			svc := utils.FileToService(test.svcFile)
			resolver := ReferenceResolver{
				lbService: svc,
			}

			// act
			servers, err := resolver.GetServers(dnsZone)
			assert.NoError(t, err)

			// assert
			assert.Equal(t, test.expectedServers, servers)
		})
	}
}

func TestGetGslbExposedIPs(t *testing.T) {
	var tests = []struct {
		name        string
		svcFile     string
		expectedIPs []string
	}{
		{
			name:        "no exposed IPs",
			svcFile:     "./testdata/lb_service_no_ips.yaml",
			expectedIPs: []string{},
		},
		{
			name:        "single exposed IP",
			svcFile:     "../testdata/lb_service.yaml",
			expectedIPs: []string{"10.0.0.1"},
		},
		{
			name:        "multiple exposed IPs",
			svcFile:     "./testdata/lb_service_multiple_ips.yaml",
			expectedIPs: []string{"10.0.0.1", "10.0.0.2"},
		},
	}
	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {
			// arrange
			svc := utils.FileToService(test.svcFile)
			resolver := ReferenceResolver{
				lbService: svc,
			}

			// act
			IPs, err := resolver.GetGslbExposedIPs([]utils.DNSServer{})
			assert.NoError(t, err)

			// assert
			assert.Equal(t, test.expectedIPs, IPs)
		})
	}
}
