import (
	"fmt"

	"github.com/k8gb-io/k8gb/controllers/refresolver/gatewayapi"
	"github.com/k8gb-io/k8gb/controllers/utils"
	gatewayapiv1 "sigs.k8s.io/gateway-api/apis/v1"
	gatewayapiv1alpha2 "sigs.k8s.io/gateway-api/apis/v1alpha2"
)

// TCPRouteAdapter adapts TCPRoute to the gatewayapi.RouteAdapter interface
type TCPRouteAdapter struct {
	tcpRoute *gatewayapiv1alpha2.TCPRoute
	hostname string
}

// NewTCPRouteAdapter creates a new TCPRoute adapter
func NewTCPRouteAdapter(tcpRoute *gatewayapiv1alpha2.TCPRoute, hostname string) *TCPRouteAdapter {
	return &TCPRouteAdapter{tcpRoute: tcpRoute, hostname: hostname}
}

func (a *TCPRouteAdapter) GetHostnames() ([]gatewayapiv1.Hostname, error) {
	if a.hostname == "" {
		return nil, fmt.Errorf("TCPRoute %s requires %s annotation", a.tcpRoute.Name, utils.HostnameAnnotation)
	}
	return []gatewayapiv1.Hostname{gatewayapiv1.Hostname(a.hostname)}, nil
}

func (a *TCPRouteAdapter) GetParentRefs() []gatewayapiv1.ParentReference {
	return a.tcpRoute.Spec.ParentRefs
}

func (a *TCPRouteAdapter) GetRules() []gatewayapi.RouteRule {
	rules := make([]gatewayapi.RouteRule, len(a.tcpRoute.Spec.Rules))
	for i := range a.tcpRoute.Spec.Rules {
		rules[i] = &TCPRouteRuleAdapter{rule: &a.tcpRoute.Spec.Rules[i]}
	}
	return rules
}

func (a *TCPRouteAdapter) GetName() string {
	return a.tcpRoute.Name
}

func (a *TCPRouteAdapter) GetNamespace() string {
	return a.tcpRoute.Namespace
}

// TCPRouteRuleAdapter adapts TCPRouteRule to the RouteRule interface
type TCPRouteRuleAdapter struct {
	rule *gatewayapiv1alpha2.TCPRouteRule
}

func (a *TCPRouteRuleAdapter) GetBackendRefs() []gatewayapiv1.BackendRef {
	return a.rule.BackendRefs
}
