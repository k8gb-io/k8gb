func TestLegacyRuntimeReconcilesBeforeMigrationRequest(t *testing.T) {
	className := testIngressClassName
	legacy := &k8gbv1beta1.Gslb{
		ObjectMeta: metav1.ObjectMeta{
			Name:      "legacy-runtime",
			Namespace: "default",
			UID:       types.UID("legacy-runtime-uid"),
		},
		Spec: k8gbv1beta1.GslbSpec{
			Ingress: k8gbv1beta1.IngressSpec{
				IngressClassName: &className,
				Rules: []k8gbv1beta1.IngressRule{
					{
						Host: "legacy-runtime.cloud.example.com",
						IngressRuleValue: k8gbv1beta1.IngressRuleValue{
							HTTP: &netv1.HTTPIngressRuleValue{
								Paths: []netv1.HTTPIngressPath{
									{
										Path:     "/",
										PathType: pathTypePrefix(),
										Backend: netv1.IngressBackend{
											Service: &netv1.IngressServiceBackend{
												Name: "frontend-podinfo",
												Port: netv1.ServiceBackendPort{Name: "http"},
											},
										},
									},
								},
							},
						},
					},
				},
			},
			Strategy: k8gbv1beta1.Strategy{
				Type: resolver.RoundRobinStrategy,
			},
		},
	}

	reconciler, cl := newLegacyRuntimeReconciler(t, legacy)
	_, err := reconciler.Reconcile(context.Background(), ctrl.Request{
		NamespacedName: types.NamespacedName{Name: legacy.Name, Namespace: legacy.Namespace},
	})
	require.NoError(t, err)

	updatedLegacy := &k8gbv1beta1.Gslb{}
	err = cl.Get(context.Background(), types.NamespacedName{Name: legacy.Name, Namespace: legacy.Namespace}, updatedLegacy)
	require.NoError(t, err)
	require.Equal(t, "legacy-runtime.cloud.example.com", updatedLegacy.Status.Hosts)
	require.Equal(t, k8gbv1beta1.NotFound, updatedLegacy.Status.ServiceHealth["legacy-runtime.cloud.example.com"])

	createdIngress := &netv1.Ingress{}
	err = cl.Get(context.Background(), types.NamespacedName{Name: legacy.Name, Namespace: legacy.Namespace}, createdIngress)
	require.NoError(t, err)

	endpoint := &externaldnsApi.DNSEndpoint{}
	err = cl.Get(context.Background(), types.NamespacedName{Name: legacy.Name, Namespace: legacy.Namespace}, endpoint)
	require.NoError(t, err)
}

func TestLegacyRuntimeSkipsRequestedMigrationObjects(t *testing.T) {
	legacy := &k8gbv1beta1.Gslb{
		ObjectMeta: metav1.ObjectMeta{
			Name:      "legacy-requested",
			Namespace: "default",
			UID:       types.UID("legacy-requested-uid"),
			Labels: map[string]string{
				migrationRequestLabelKey: "true",
			},
		},
		Spec: k8gbv1beta1.GslbSpec{
			Strategy: k8gbv1beta1.Strategy{
				Type: resolver.RoundRobinStrategy,
			},
		},
	}

	reconciler, cl := newLegacyRuntimeReconciler(t, legacy)
	_, err := reconciler.Reconcile(context.Background(), ctrl.Request{
		NamespacedName: types.NamespacedName{Name: legacy.Name, Namespace: legacy.Namespace},
	})
	require.NoError(t, err)

	endpoint := &externaldnsApi.DNSEndpoint{}
	err = cl.Get(context.Background(), types.NamespacedName{Name: legacy.Name, Namespace: legacy.Namespace}, endpoint)
	require.True(t, apierrors.IsNotFound(err))
}

func newLegacyRuntimeReconciler(t *testing.T, objs ...client.Object) (*LegacyGslbReconciler, client.Client) {
	t.Helper()
	ctrl := gomock.NewController(t)
	defer ctrl.Finish()
	s := runtime.NewScheme()
	require.NoError(t, clientgoscheme.AddToScheme(s))
	require.NoError(t, k8gbv1beta1.AddToScheme(s))
	require.NoError(t, k8gbv1beta1io.AddToScheme(s))
	require.NoError(t, externaldnsApi.AddToScheme(s))

	cl := fake.NewClientBuilder().
		WithScheme(s).
		WithObjects(objs...).
		WithStatusSubresource(&k8gbv1beta1.Gslb{}).
		Build()
	zoneService := zones.NewMockZoneDelegation(ctrl)
	zoneService.EXPECT().HasAvailableIPs(gomock.Any()).Return(true).AnyTimes()
	recorder := record.NewFakeRecorder(10)
	reconciler := &LegacyGslbReconciler{
		Client:      cl,
		Scheme:      s,
		Recorder:    recorder,
		ZoneService: zoneService,
		Logger:      logging.Logger(),
		Config: &resolver.Config{
			ClusterGeoTag:           "eu",
			ReconcileRequeueSeconds: 1,
			DelegationZones: resolver.DelegationZones{
				&resolver.DelegationZoneInfo{
					LoadBalancedZone:  "cloud.example.com",
					ParentZone:        "example.com",
					ClusterNSName:     "gslb-ns-eu-cloud.example.com",
					ExtClusterNSNames: map[string]string{},
				},
			},
		},
	}

	return reconciler, cl
}

func pathTypePrefix() *netv1.PathType {
	p := netv1.PathTypePrefix
	return &p
}
