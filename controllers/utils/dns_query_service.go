import (
	"fmt"

	"github.com/miekg/dns"
)

type DNSQueryService interface {
	Query(host string, nameservers DNSList) (*dns.Msg, error)

	ExtractARecords(msg *dns.Msg) []string
}

type DNSQueryServiceImpl struct {
}

func NewDNSQueryService() *DNSQueryServiceImpl {
	return &DNSQueryServiceImpl{}
}

func (i *DNSQueryServiceImpl) Query(host string, nameservers DNSList) (*dns.Msg, error) {
	dnsMsg := new(dns.Msg)
	fqdn := fmt.Sprintf("%s.", host) // Convert to true FQDN with dot at the end
	dnsMsg.SetQuestion(fqdn, dns.TypeA)
	dnsMsgA, err := Exchange(dnsMsg, nameservers)
	return dnsMsgA, err
}

func (i *DNSQueryServiceImpl) ExtractARecords(msg *dns.Msg) []string {
	var ARecords []string
	for _, nsA := range msg.Answer {
		ip := nsA.(*dns.A).A.String()
		ARecords = append(ARecords, ip)
	}
	return ARecords
}
