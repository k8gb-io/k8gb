import (
	"context"
	"fmt"
	"time"
)

func Wait(minutes int32, readyFunc func() (bool, error)) error {
	ch := make(chan string)
	errch := make(chan error)
	ctx, cancel := context.WithTimeout(context.Background(), time.Duration(minutes)*time.Minute)
	defer cancel()
	go func() {
		for range time.NewTicker(5 * time.Second).C {
			ready, err := readyFunc()
			if err != nil {
				errch <- err
			}
			if ready {
				break
			}
		}
		ch <- "Done"
	}()
	select {
	case <-ctx.Done():
		return fmt.Errorf("timeout! %v minutes expired", minutes)
	case <-ch:
		return nil
	case <-errch:
		return fmt.Errorf("wait error")
	}
}
