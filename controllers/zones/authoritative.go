import (
	"context"
	"fmt"
)

// AuthoritativeServer keep information about CoreDNS in particular clusters
type AuthoritativeServer struct {
	IP      string
	GeoTag  string
	IsLocal bool
}

type AuthoritativeServers map[string]AuthoritativeServer

// ResolveAuthoritativeServersFromZoneDelegations resolves authoritative CoreDNS
// endpoints for a given host using ZoneDelegation status instead of performing
// external DNS (Glue A) lookups.
//
// It searches for a ZoneDelegation matching the host's zone and returns all
// authoritative DNS servers (name -> {IP, GeoTag}) from status.dnsServers,
// enriched with GeoTag information derived from configuration.
//
// The function returns the complete set of authoritative servers for the zone,
// including both local and remote clusters. Filtering (e.g. excluding local
// cluster servers) is expected to be handled by the caller if needed.
//
// Example:
// Given the following ZoneDelegation:
//
//	apiVersion: k8gb.io/v1beta1
//	kind: ZoneDelegation
//	metadata:
//	  name: cloud-example-com.conf
//	spec:
//	  loadBalancedZone: cloud.example.com
//	status:
//	  dnsServers:
//	  - name: gslb-ns-eu-cloud.example.com
//	    address: 172.18.0.6   // local cluster
//	  - name: gslb-ns-eu-cloud.example.com
//	    address: 172.18.0.7   // local cluster
//	  - name: gslb-ns-us-cloud.example.com
//	    address: 172.18.0.11  // remote cluster
//	  - name: gslb-ns-us-cloud.example.com
//	    address: 172.18.0.12  // remote cluster
//
// For host: "app.cloud.example.com"
//
// Expected result:
//
//	AuthoritativeServers{
//	  "gslb-ns-eu-cloud.example.com": {IP: "172.18.0.6", GeoTag: "eu"},
//	  "gslb-ns-us-cloud.example.com": {IP: "172.18.0.11", GeoTag: "us"},
//	}
//
// Note: If multiple IPs exist for the same DNS server name, only one
// representative address is returned.
func (z *ZoneDelegationImpl) ResolveAuthoritativeServersFromZoneDelegations(ctx context.Context, host string) (AuthoritativeServers, error) {
	l, err := z.List(ctx)
	if err != nil {
		return nil, err
	}
	found, zone := l.FindZone(host)
	if !found {
		return nil, fmt.Errorf("ZoneDelegation for %s not found", host)
	}
	geoTags := z.config.GetTagsByNSNames(zone.Spec.LoadBalancedZone, zone.Spec.ParentZone)
	authoritativeServers := make(AuthoritativeServers)
	for _, dnsServer := range zone.Status.DNSServers {
		geotag, found := geoTags[dnsServer.Name]
		if !found {
			return nil, fmt.Errorf("ZoneDelegation for %s not found geotag for NS %s", host, dnsServer.Name)
		}
		isLocal := geotag == z.config.ClusterGeoTag
		authoritativeServers[dnsServer.Name] = AuthoritativeServer{IP: dnsServer.Address, GeoTag: geotag, IsLocal: isLocal}
	}
	return authoritativeServers, nil
}

func (a AuthoritativeServers) GetExternalAuthoritativeServers() AuthoritativeServers {
	externals := make(AuthoritativeServers)
	for k, v := range a {
		if !v.IsLocal {
			externals[k] = v
		}
	}
	return externals
}
