func equalDNSServers(a, b []v1beta1.DNSServer) bool {
	if len(a) != len(b) {
		return false
	}
	toMap := func(s []v1beta1.DNSServer) map[string]struct{} {
		m := make(map[string]struct{}, len(s))
		for _, v := range s {
			m[v.Name+"|"+v.Address] = struct{}{}
		}
		return m
	}
	am, bm := toMap(a), toMap(b)
	for k := range am {
		if _, ok := bm[k]; !ok {
			return false
		}
	}
	return true
}

func equalZoneDelegationItems(a, b []v1beta1.ZoneDelegation) error {
	if len(a) != len(b) {
		return fmt.Errorf("different number of items: expected %d, got %d", len(a), len(b))
	}

	toMap := func(items []v1beta1.ZoneDelegation) map[string]v1beta1.ZoneDelegation {
		m := make(map[string]v1beta1.ZoneDelegation, len(items))
		for _, item := range items {
			m[item.Name] = item
		}
		return m
	}

	am := toMap(a)
	bm := toMap(b)

	for name, expected := range am {
		actual, ok := bm[name]
		if !ok {
			return fmt.Errorf("missing ZoneDelegation %q", name)
		}

		if expected.Spec.LoadBalancedZone != actual.Spec.LoadBalancedZone {
			return fmt.Errorf("%s: LoadBalancedZone mismatch: expected %q, got %q",
				name, expected.Spec.LoadBalancedZone, actual.Spec.LoadBalancedZone)
		}
		if expected.Spec.ParentZone != actual.Spec.ParentZone {
			return fmt.Errorf("%s: ParentZone mismatch: expected %q, got %q",
				name, expected.Spec.ParentZone, actual.Spec.ParentZone)
		}
		if expected.Spec.DNSZoneNegTTL != actual.Spec.DNSZoneNegTTL {
			return fmt.Errorf("%s: DNSZoneNegTTL mismatch: expected %d, got %d",
				name, expected.Spec.DNSZoneNegTTL, actual.Spec.DNSZoneNegTTL)
		}

		if !equalDNSServers(expected.Status.DNSServers, actual.Status.DNSServers) {
			return fmt.Errorf("%s: DNSServers mismatch: expected %+v, got %+v",
				name, expected.Status.DNSServers, actual.Status.DNSServers)
		}
	}

	return nil
}
