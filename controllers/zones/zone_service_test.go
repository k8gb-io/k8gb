func equalDNSServers(a, b []v1beta1.DNSServer) bool {
	if len(a) != len(b) {
		return false
	}
	toMap := func(s []v1beta1.DNSServer) map[string]struct{} {
		m := make(map[string]struct{}, len(s))
		for _, v := range s {
			m[v.Name+"|"+v.Address] = struct{}{}
		}
		return m
	}
	am, bm := toMap(a), toMap(b)
	for k := range am {
		if _, ok := bm[k]; !ok {
			return false
		}
	}
	return true
}
