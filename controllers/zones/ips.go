import (
	"bytes"
	"net"
	"sort"
)

type ZoneDelegationIPs []string

// Sorted returns list of IPs sorted by IP number; 10.0.0.2 < 10.0.0.10
func (ips ZoneDelegationIPs) Sorted() []string {
	sorted := make([]string, len(ips))
	copy(sorted, ips)

	// 10.0.0.2 < 10.0.0.10
	sort.Slice(sorted, func(i, j int) bool {
		return bytes.Compare(net.ParseIP(sorted[i]), net.ParseIP(sorted[j])) < 0
	})

	return sorted
}

func (ips ZoneDelegationIPs) Unsorted() []string {
	return ips
}
