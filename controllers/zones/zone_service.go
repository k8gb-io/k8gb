type ZoneDelegation interface {
	List(ctx context.Context) (*v1beta1.ZoneDelegationList, error)
	Get(ctx context.Context, objKey client.ObjectKey) (*v1beta1.ZoneDelegation, error)
	Save(ctx context.Context, z *v1beta1.ZoneDelegation) error
	Delete(ctx context.Context, z *v1beta1.ZoneDelegation) error
	ListConfigZoneDelegations(ctx context.Context) (*v1beta1.ZoneDelegationList, error)
	ListAllZoneDelegations(ctx context.Context) (*v1beta1.ZoneDelegationList, error)
	AvailableIPs(ctx context.Context) (ZoneDelegationIPs, error)
	HasAvailableIPs(ctx context.Context) bool
	HasExtClusterGeoTags(ctx context.Context) bool
	UpdateStatus(ctx context.Context, zd *v1beta1.ZoneDelegation) error
	ExtendedZoneDelegation(ctx context.Context, zd *v1beta1.ZoneDelegation) (*ExtendedZoneDelegation, error)
	ResolveAuthoritativeServersFromZoneDelegations(ctx context.Context, host string) (AuthoritativeServers, error)
	RemoveZoneDelegation(ctx context.Context, zd *v1beta1.ZoneDelegation) error
}

type ZoneDelegationImpl struct {
	client     client.Client
	config     *resolver.Config
	ipresolver ipresolver.Resolver
	apiReader  client.Reader
}

func NewZoneDelegationImpl(
	client client.Client,
	apiReader client.Reader,
	config *resolver.Config,
	bootstrapSvc ipresolver.Resolver) *ZoneDelegationImpl {
	return &ZoneDelegationImpl{client: client, config: config, apiReader: apiReader, ipresolver: bootstrapSvc}
}

func (z *ZoneDelegationImpl) List(ctx context.Context) (*v1beta1.ZoneDelegationList, error) {
	list := &v1beta1.ZoneDelegationList{}
	err := z.client.List(ctx, list)
	if err != nil {
		return nil, fmt.Errorf("error listing zones: %v", err)
	}
	return list, nil
}

func (z *ZoneDelegationImpl) Get(ctx context.Context, objKey client.ObjectKey) (*v1beta1.ZoneDelegation, error) {
	delegationZone := &v1beta1.ZoneDelegation{}
	err := z.client.Get(ctx, objKey, delegationZone)
	return delegationZone, err
}

// Save creates or updates ZoneDelegation, updates apart status.
// Updates coreDNS configuration.
func (z *ZoneDelegationImpl) Save(ctx context.Context, zd *v1beta1.ZoneDelegation) error {
	current := &v1beta1.ZoneDelegation{
		ObjectMeta: metav1.ObjectMeta{
			Name: zd.Name,
		},
	}
	op, err := controllerutil.CreateOrUpdate(ctx, z.client, current, func() error {
		// Only manage fields owned by this controller.
		current.Spec.LoadBalancedZone = zd.Spec.LoadBalancedZone
		current.Spec.ParentZone = zd.Spec.ParentZone
		current.Spec.DNSZoneNegTTL = zd.Spec.DNSZoneNegTTL
		return nil
	})
	if err != nil {
		return fmt.Errorf("error creating/updating zone delegation: %w", err)
	}

	// Only update CoreDNS if something actually changed
	if op != controllerutil.OperationResultNone {
		if err := z.updateCoreDNSConfiguration(ctx, *current); err != nil {
			return fmt.Errorf("error updating CoreDNS configuration: %w", err)
		}
	}

	return nil
}

func (z *ZoneDelegationImpl) Delete(ctx context.Context, zone *v1beta1.ZoneDelegation) error {
	if err := z.client.Delete(ctx, zone); err != nil {
		if errors.IsNotFound(err) {
			return nil
		}
		return fmt.Errorf("failed to delete ZoneDelegation %s: %w", zone.Name, err)
	}
	return nil
}

// ListConfigZoneDelegations reads internal resolver.Config and gets ZoneDelegationList
func (z *ZoneDelegationImpl) ListConfigZoneDelegations(ctx context.Context) (*v1beta1.ZoneDelegationList, error) {
	ips, err := z.AvailableIPs(ctx)
	if err != nil {
		return nil, err
	}
	zoneDelegationList := &v1beta1.ZoneDelegationList{}
	for _, dzi := range z.config.DelegationZones {
		zd := v1beta1.ZoneDelegation{}
		zd.Spec.LoadBalancedZone = dzi.LoadBalancedZone
		zd.Spec.ParentZone = dzi.ParentZone
		zd.Spec.DNSZoneNegTTL = dzi.NegativeTTL
		zd.Status.DNSServers = []v1beta1.DNSServer{}
		for _, ns := range dzi.GetNSServerList() {
			for _, ip := range ips {
				zd.Status.DNSServers = append(zd.Status.DNSServers, v1beta1.DNSServer{Name: ns, Address: ip})
			}
		}
		zd.Name = name(zd)
		zoneDelegationList.Items = append(zoneDelegationList.Items, zd)
	}
	return zoneDelegationList, nil
}

// ListAllZoneDelegations list existing zone delegations and config zone delegations and merge them.
// Existing zones has precedence and overrides config ones
func (z *ZoneDelegationImpl) ListAllZoneDelegations(ctx context.Context) (*v1beta1.ZoneDelegationList, error) {
	finalList := &v1beta1.ZoneDelegationList{}
	finalMap := make(map[string]v1beta1.ZoneDelegation)

	cfgList, err := z.ListConfigZoneDelegations(ctx)
	if err != nil {
		return nil, err
	}
	currentList, err := z.List(ctx)
	if err != nil {
		return nil, err
	}

	for _, cfg := range cfgList.Items {
		finalMap[name(cfg)] = cfg
	}

	for _, current := range currentList.Items {
		finalMap[name(current)] = current
	}

	for _, final := range finalMap {
		finalList.Items = append(finalList.Items, final)
	}

	return finalList, nil
}

func (z *ZoneDelegationImpl) UpdateStatus(ctx context.Context, zd *v1beta1.ZoneDelegation) error {
	current, err := z.Get(ctx, types.NamespacedName{
		Name: zd.Name,
	})
	if err != nil {
		if errors.IsNotFound(err) {
			return fmt.Errorf("zone delegation %s not found", zd.Name)
		}
		return fmt.Errorf("error getting zone delegation: %w", err)
	}

	detail, err := NewZoneDelegationWrapper(current, z.config, z.ipresolver).GetDetail(ctx)
	if err != nil {
		return err
	}

	desiredStatus, err := z.buildDesiredStatus(ctx, *detail)
	if err != nil {
		return err
	}

	if equalStatus(current.Status, desiredStatus) {
		return nil
	}

	current.Status = desiredStatus
	if err := z.client.Status().Update(ctx, current); err != nil {
		return fmt.Errorf("error updating zone delegation status: %w", err)
	}

	return nil
}

func equalStatus(a, b v1beta1.ZoneDelegationStatus) bool {
	if len(a.DNSServers) != len(b.DNSServers) {
		return false
	}
	toCountMap := func(servers []v1beta1.DNSServer) map[string]int {
		m := make(map[string]int, len(servers))
		for _, s := range servers {
			key := s.Name + "|" + s.Address
			m[key]++
		}
		return m
	}

	am := toCountMap(a.DNSServers)
	bm := toCountMap(b.DNSServers)

	if len(am) != len(bm) {
		return false
	}
	for k, av := range am {
		bv, ok := bm[k]
		if !ok || av != bv {
			return false
		}
	}
	return true
}

func (z *ZoneDelegationImpl) buildDesiredStatus(ctx context.Context, exzd ExtendedZoneDelegation) (v1beta1.ZoneDelegationStatus, error) {
	status := v1beta1.ZoneDelegationStatus{
		DNSServers: make([]v1beta1.DNSServer, 0),
	}

	glueAList := z.ipresolver.GetClusterGlueAResults(ctx, exzd.LoadBalancedZone, exzd.ParentZone).FilterResolvedRecords()

	if glueAList.LocalClusterError() != nil {
		return v1beta1.ZoneDelegationStatus{}, glueAList.LocalClusterError()
	}

	for _, gluea := range glueAList {
		status.DNSServers = append(status.DNSServers, v1beta1.DNSServer{
			Name:    gluea.Cluster,
			Address: gluea.IP,
		})
	}

	sort.Slice(status.DNSServers, func(i, j int) bool {
		if status.DNSServers[i].Name != status.DNSServers[j].Name {
			return status.DNSServers[i].Name < status.DNSServers[j].Name
		}
		return status.DNSServers[i].Address < status.DNSServers[j].Address
	})
	return status, nil
}

// AvailableIPs gets exposed IPs from the cluster
func (z *ZoneDelegationImpl) AvailableIPs(ctx context.Context) (ZoneDelegationIPs, error) {
	exposedIPs, err := z.ipresolver.GetExposedIPs(ctx)
	if err != nil {
		return nil, err
	}
	return exposedIPs.IPs, nil
}

// HasAvailableIPs returns false if any error during IP reading or IPs are not available yet
func (z *ZoneDelegationImpl) HasAvailableIPs(ctx context.Context) bool {
	exposedIPs, err := z.AvailableIPs(ctx)
	if err != nil {
		return false
	}
	return len(exposedIPs) != 0
}

func (z *ZoneDelegationImpl) HasExtClusterGeoTags(ctx context.Context) bool {
	l, err := z.List(ctx)
	if err != nil {
		return false
	}
	if len(l.Items) == 0 {
		return false
	}
	detail, err := NewZoneDelegationWrapper(&l.Items[0], z.config, z.ipresolver).GetDetail(ctx)
	if err != nil {
		return false
	}
	return len(detail.ExtClusterNSNames) > 0
}

func (z *ZoneDelegationImpl) ExtendedZoneDelegation(ctx context.Context, zd *v1beta1.ZoneDelegation) (*ExtendedZoneDelegation, error) {
	return NewZoneDelegationWrapper(zd, z.config, z.ipresolver).GetDetail(ctx)
}

func (z *ZoneDelegationImpl) RemoveZoneDelegation(ctx context.Context, zd *v1beta1.ZoneDelegation) error {
	err := z.Delete(ctx, zd)
	if err != nil {
		return err
	}

	// remove configuration from coreDNS config
	if err = z.updateCoreDNSConfiguration(ctx, *zd); err != nil {
		return fmt.Errorf("error updating CoreDNS configuration: %w", err)
	}

	return err
}

// updateCoreDNSConfiguration creates, updates, or skips the k8gb-zone-delegation ConfigMap.
// controllerutil.CreateOrUpdate was avoided to keep full control over update conditions (DeepEqual is too coarse).
func (z *ZoneDelegationImpl) updateCoreDNSConfiguration(ctx context.Context, zd v1beta1.ZoneDelegation) error {
	const zoneCM = "k8gb-zone-delegation"

	coreDNSZones := &corev1.ConfigMap{}
	err := z.apiReader.Get(ctx, types.NamespacedName{Namespace: z.config.K8gbNamespace, Name: zoneCM}, coreDNSZones)
	if err != nil {
		return fmt.Errorf("failed to get ConfigMap %s: %w", zoneCM, err)
	}

	if coreDNSZones.Data == nil {
		coreDNSZones.Data = make(map[string]string)
	}

	// update coreDNS data if necessary
	zoneKey := name(zd)

	// Skip update if the zone entry already exists.
	if _, found := coreDNSZones.Data[zoneKey]; found {
		return nil
	}

	// If the ZoneDelegation is being deleted, remove its zone entry from the CoreDNS ConfigMap.
	// Otherwise, ensure the zone entry exists/gets updated with the desired CoreDNS configuration.
	if zd.IsInDeletion() {
		delete(coreDNSZones.Data, zoneKey)
	} else {
		configData := getCoreDNSData(zd.Spec.LoadBalancedZone)
		coreDNSZones.Data[zoneKey] = configData
	}

	return z.client.Update(ctx, coreDNSZones)
}

func getCoreDNSData(zone string) string {
	const (
		zoneTemplate = `%s:5353 {
  import k8gbplugins
}
`
	)
	return fmt.Sprintf(zoneTemplate, zone)
}

func name(zd v1beta1.ZoneDelegation) string {
	return fmt.Sprintf("%s.conf", strings.ReplaceAll(zd.Spec.LoadBalancedZone, ".", "-"))
}
