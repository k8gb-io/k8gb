type CoreDNSReconciler struct {
	client.Client
	Config      *resolver.Config
	Recorder    record.EventRecorder
	Tracer      trace.Tracer
	Scheme      *runtime.Scheme
	DNSProvider dns.Provider
	ZoneService zones.ZoneDelegation
	IPResolver  ipresolver.Resolver
	Logger      *zerolog.Logger
}

func (r *CoreDNSReconciler) Reconcile(ctx context.Context, _ ctrl.Request) (ctrl.Result, error) {
	// todo: introduce variable for reconciliation interval
	var err error
	result := utils.NewReconcileResultHandler(0)
	r.Logger.Info().Msg("Reconciling CoreDNS delegation")

	ips, err := r.IPResolver.GetExposedIPs(ctx)
	if err != nil {
		if strings.Contains(err.Error(), "no LoadBalancer ExternalIPs are found") {
			log.Info().Msg("Waiting for IPs to be assigned to the CoreDNS service")
			return result.Stop()
		}
		r.Logger.Err(err).Msg("Error processing IP source for CoreDNS")
		return result.Stop()
	}

	r.Logger.Info().Msgf("available IPs: [%s]", strings.Join(ips.IPs, ","))
	if len(ips.IPs) == 0 {
		r.Logger.Info().Msg("Waiting for IPs to be assigned.")
		// When IP's are assigned, status of origin source has been changed and reconciliation executed again
		return result.Stop()
	}

	// TODO: remove this after refactoring applicationDNSEndpoints
	r.Config.DelegationZones.SetIPs(ips.IPs)

	// list existing ZoneDelegations and config DelegationZones
	zd, err := r.ZoneService.ListAllZoneDelegations(ctx)
	if err != nil {
		r.Logger.Err(err).Msg("Error creating zone delegation")
		return result.RequeueError(err)
	}

	for _, zoneDelegation := range zd.Items {
		// save zoneDelegation
		err = r.ZoneService.Save(ctx, &zoneDelegation)
		if err != nil {
			r.Logger.Err(err).Msg("Error creating zone delegation resource")
			return result.RequeueError(err)
		}
		r.Logger.Info().Str("name", zoneDelegation.Name).Msg("ZoneDelegation saved")

		detail, err := zones.NewZoneDelegationWrapper(&zoneDelegation, r.Config, r.IPResolver).GetDetail(ctx)
		if err != nil {
			r.Logger.Err(err).Msg("Error getting zone delegation data")
			return result.RequeueError(err)
		}

		// Create zoneDelegation in edge DNS
		r.Logger.Info().
			Str("NS record", detail.GetExternalDNSEndpointName()).
			Str("ZoneDelegation", zoneDelegation.Name).
			Str("Provider", r.DNSProvider.String()).
			Msg("Calling DNS provider")
		err = r.DNSProvider.CreateZoneDelegation(detail)
		if err != nil {
			r.Logger.Err(err).Msg("Error creating zone delegation")
		}
	}

	r.Logger.Info().Msg("Finished Reconciling CoreDNS delegation")
	return result.Stop()
}
